    "        return 0\n",
    "\n",
    "    wh_per_minute = charge_rate_wh_per_hour / 60.0\n",
    "    return int(math.ceil(energy_wh / wh_per_minute))\n",
    "\n",
    "\n",
    "def block_start_time(block: Block, trips: Dict[int, dict]) -> int:\n",
    "    return int(trips[block[0]][\"start\"])\n",
    "\n",
    "\n",
    "def block_end_time(block: Block, trips: Dict[int, dict]) -> int:\n",
    "    return int(trips[block[-1]][\"end\"])\n",
