    "\n",
    "def arc_duration(arc_dict: dict, key, default: int = 0) -> int:\n",
    "    arc = arc_dict.get(key)\n",
    "    if arc is None:\n",
    "        return default\n",
    "\n",
    "    data = arc[-1]\n",
    "    return int(data.get(\"duration\", 0))\n",
