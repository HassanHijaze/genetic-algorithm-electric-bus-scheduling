    "def stitch_blocks_by_matching(\n",
    "    adjacency: Dict[int, List[int]],\n",
    "    number_of_blocks: int,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Convert selected block merges into duties.\n",
    "\n",
