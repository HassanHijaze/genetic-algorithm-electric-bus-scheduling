    "    nodes = list(range(number_of_blocks))\n",
    "    pair_left, _ = hopcroft_karp(nodes, nodes, adjacency)\n",
    "\n",
    "    successor = {\n",
    "        u: v\n",
    "        for u, v in pair_left.items()\n",
    "        if v is not None\n",
    "    }\n",
    "\n",
    "    predecessor = {\n",
    "        v: u\n",
    "        for u, v in successor.items()\n",
    "    }\n",
    "\n",
    "    heads = [\n",
    "        node\n",
    "        for node in nodes\n",
    "        if node not in predecessor\n",
    "    ]\n",
    "\n",
    "    duties: List[List[int]] = []\n",
    "\n",
    "    for head in heads:\n",
    "        duty = [head]\n",
    "        current = head\n",
    "\n",
    "        while current in successor:\n",
    "            current = successor[current]\n",
