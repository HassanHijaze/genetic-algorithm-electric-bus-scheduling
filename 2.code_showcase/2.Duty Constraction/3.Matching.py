    "\n",
    "    def bfs() -> bool:\n",
    "        queue = deque()\n",
    "        found_path = False\n",
    "\n",
    "        for u in left_nodes:\n",
    "            if pair_left[u] is None:\n",
    "                dist[u] = 0\n",
    "                queue.append(u)\n",
    "            else:\n",
    "                dist[u] = float(\"inf\")\n",
    "\n",
    "        while queue:\n",
    "            u = queue.popleft()\n",
    "\n",
    "            for v in adjacency.get(u, []):\n",
    "                matched_u = pair_right[v]\n",
    "\n",
    "                if matched_u is None:\n",
    "                    found_path = True\n",
    "                elif dist[matched_u] == float(\"inf\"):\n",
    "                    dist[matched_u] = dist[u] + 1\n",
    "                    queue.append(matched_u)\n",
    "\n",
    "        return found_path\n",
