    "def _trip_energy(trips: Dict[int, dict], t: int) -> float:\n",
    "    try:\n",
    "        return float(trips[t].get(\"energy\", 0.0))\n",
    "    except Exception:\n",
    "        return 0.0\n",
    "\n",
    "def _tt_energy_duration(tt_best, u: int, v: int) -> Tuple[float, int]:\n",
    "    dh = tt_best.get((u, v))\n",
    "    if not dh:\n",
    "        return (float(\"inf\"), 10**9)\n",
    "    d = dh[1]\n",
    "    return (float(d.get(\"energy\", 0.0)), int(d.get(\"duration\", 0)))\n",
    "\n",
    "def _natural_depot_for_trip(t: int, dt_best, td_best, default_depot: int = 0) -> int:\n",
    "    \"\"\"Prefer a depot that has BOTH type-1 to head and type-2 from tail if possible.\"\"\"\n",
    "    dep_in = int(dt_best[t][0]) if t in dt_best else None\n",
    "    dep_out = int(td_best[t][0]) if t in td_best else None\n",
    "    if dep_in is not None and dep_out is not None and dep_in == dep_out:\n",
    "        return dep_in\n",
    "    if dep_in is not None:\n",
    "        return dep_in\n",
    "    if dep_out is not None:\n",
    "        return dep_out\n",
    "    return default_depot\n",
    "\n",
    "def _is_tt_feasible(\n",
    "    u: int, v: int,\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    allow_equal: bool = ALLOW_EQUAL_TIME,\n",
    ") -> bool:\n",
    "    dh = tt_best.get((u, v))\n",
    "    if not dh:\n",
    "        return False\n",
    "    dur = int(dh[1].get(\"duration\", 0))\n",
    "    arrive = int(trips[u][\"end\"]) + dur\n",
    "    start_v = int(trips[v][\"start\"])\n",
    "    return (arrive <= start_v) if allow_equal else (arrive < start_v)\n",
    "\n",
    "def _repair_route_into_chains(\n",
    "    route: List[int],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    allow_equal: bool = ALLOW_EQUAL_TIME,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Split a proposed route into one or more time-feasible chains.\n",
    "    Removes unknown/duplicate trip IDs and breaks wherever TT is infeasible.\n",
    "    \"\"\"\n",
    "    seen: Set[int] = set()\n",
    "    cleaned: List[int] = []\n",
    "    for t in route:\n",
    "        if t in trips and t not in seen:\n",
    "            seen.add(t)\n",
    "            cleaned.append(t)\n",
    "    if not cleaned:\n",
    "        return []\n",
    "\n",
    "    chains: List[List[int]] = []\n",
    "    cur = [cleaned[0]]\n",
    "    for u, v in zip(cleaned, cleaned[1:]):\n",
    "        if _is_tt_feasible(u, v, trips, tt_best, allow_equal):\n",
    "            cur.append(v)\n",
    "        else:\n",
    "            chains.append(cur)\n",
    "            cur = [v]\n",
    "    chains.append(cur)\n",
    "    return chains\n",
