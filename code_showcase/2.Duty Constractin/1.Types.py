    "def _per_bus_wh_per_min() -> float:\n",
    "    return CHARGE_RATE_WH_PER_HOUR / 60.0\n",
    "\n",
    "def _grid_cap_at_minute(grid_limits: List[Tuple[int, Optional[float]]], minute_idx: int) -> Optional[int]:\n",
    "    \"\"\"\n",
    "    Returns max concurrent buses allowed at this minute (integer), or None if unlimited.\n",
    "    grid_limits: list of (end_time_sec, limit_wh_per_minute or None)\n",
    "    \"\"\"\n",
    "    t = minute_idx * 60\n",
    "    for end_t, lim in grid_limits:\n",
    "        if end_t == -1 or t <= end_t:\n",
    "            if lim is None:\n",
    "                return None\n",
    "            per_bus = _per_bus_wh_per_min()\n",
    "            if per_bus <= 0:\n",
    "                return 0\n",
    "            return int(math.floor(lim / per_bus + 1e-9))\n",
    "    return None  # fallback unlimited\n",
    "\n",
    "# -----------------------------------------------------------------------------\n",
    "# 9) CAPACITY-AWARE SCHEDULER (stations + grid_access)\n",
    "# -----------------------------------------------------------------------------\n",
    "\n",
    "\n",
    "def schedule_station_dwells_with_capacity(\n",
    "    duties: List[List[int]],\n",
    "    blocks: List[List[int]],                      # kept for signature parity; unused here\n",
    "    merge_info: Dict[Tuple[int,int], dict],\n",
    "    stations: Dict[int, dict],                    # {sid: {\"depot_ID\": int, \"capacity\": int, \"grid_access_ID\": int}}\n",
    "    deadheads: Dict[int, dict],                   # kept for signature parity; unused here\n",
    "    grid_access: Optional[Dict[int, List[Tuple[int, Optional[float]]]]] = None,\n",
    "    battery_capacity: float = 0.0,\n",
    "    charge_rate_wh_per_hour: float =60000.0,\n",
    "    occupancy_pad_sec: int = 0,                   # extra pad minutes also consume grid/station capacity\n",
    "    *,\n",
    "    power_limits_by_id: Optional[Dict[int, List[Any]]] = None,  # raw limits override grid_access if given\n",
    "    default_access_id: int = 0,                   # fallback grid id if station lacks one\n",
    "    vt_id: int = 0,\n",
    "    rand_seed: int = 0,\n",
    "    apply_station_pad: bool = True,               # if True, station slot is occupied during pad too\n",
    "    fill_window_to_battery: bool = True,          # True: take as many minutes as fit up to battery; False: only vetted need\n",
    "):\n",
    "    \"\"\"\n",
    "    Minute-quantized scheduler with:\n",
    "      • Per-grid-ID power caps (not a single shared grid).\n",
    "      • Start-minute blackout semantics (cap applies to minute starting at t).\n",
    "      • Per-minute station capacity AND per-lane (slot) assignment, with optional pad.\n",
    "      • Splits dwells across blackout windows and capacity gaps.\n",
    "      • Energy credited strictly by full charged minutes (end-exclusive).\n",
    "\n",
    "    EXTRA GUARANTEE (drift clamp):\n",
    "      For each duty, the event's start_energy_float is clamped to never exceed the\n",
    "      last end-of-dwell SoC for that duty. This keeps printed SoC consistent and\n",
    "      prevents “teleport” increases before a dwell within the same duty.\n",
    "    \"\"\"\n",
    "    import math, random\n",
    "    rnd = random.Random(rand_seed)\n",
    "    MINUTE = 60\n",
    "    min_dwell_min = int(globals().get(\"MIN_CHARGE_MINUTES\", 20))\n",
    "\n",
    "    # ---------- helpers ----------\n",
    "    def _ceil_minute(t: int) -> int:\n",
    "        return ((int(t) + MINUTE - 1) // MINUTE) * MINUTE\n",
    "\n",
    "    def _coerce_lim(v) -> Optional[float]:\n",
    "        if v is None:\n",
    "            return None\n",
    "        if isinstance(v, (int, float)):\n",
    "            return float(v)\n",
    "        s = str(v).strip()\n",
    "        if s in {\"-\", \"\", \"None\", \"none\", \"UNLIMITED\", \"unlimited\"}:\n",
    "            return None\n",
    "        return float(s)\n",
    "\n",
    "    def _parse_tok(tok) -> Tuple[int, Optional[float]]:\n",
    "        if isinstance(tok, (list, tuple)) and len(tok) == 2:\n",
    "            return int(tok[0]), _coerce_lim(tok[1])\n",
    "        s = str(tok).strip().strip(\"[]()\")\n",
    "        a, b = [x.strip() for x in s.split(\",\")]\n",
    "        return int(a), _coerce_lim(b)\n",
    "\n",
    "    def _sorted_limits(lims: List[Tuple[int, Optional[float]]]) -> List[Tuple[int, Optional[float]]]:\n",
    "        return sorted(lims, key=lambda x: (10**15 if int(x[0]) == -1 else int(x[0])))\n",
    "\n",
    "    def _normalize_grid_limits_start_minute(lims: List[Tuple[int, Optional[float]]]) -> List[Tuple[int, Optional[float]]]:\n",
    "        if not lims:\n",
    "            return [(-1, None)]\n",
    "        lims = _sorted_limits(lims[:])\n",
    "        shifted = []\n",
    "        last_lim = lims[0][1]\n",
    "        for end_t, lim in lims:\n",
    "            e = int(end_t)\n",
    "            last_lim = lim\n",
    "            if e != -1:\n",
    "                e = max(0, e - MINUTE)  # move to start-minute semantics\n",
    "            shifted.append((e, lim))\n",
    "        if shifted[-1][0] != -1:\n",
    "            shifted.append((-1, last_lim))\n",
    "        out: List[Tuple[int, Optional[float]]] = []\n",
    "        for i, (e, l) in enumerate(shifted):\n",
    "            if i and out[-1][0] != -1 and (e != -1) and out[-1][0] >= e:\n",
    "                out[-1] = (max(0, e - MINUTE), out[-1][1])\n",
    "            out.append((e, l))\n",
    "        return out\n",
    "\n",
    "    def _build_grid_access_from_raw(pl_by_id: Dict[int, List[Any]]) -> Dict[int, List[Tuple[int, Optional[float]]]]:\n",
    "        out: Dict[int, List[Tuple[int, Optional[float]]]] = {}\n",
    "        for ga_id, seq in pl_by_id.items():\n",
    "            raw = [_parse_tok(t) for t in seq]\n",
    "            out[int(ga_id)] = _normalize_grid_limits_start_minute(raw)\n",
    "        return out\n",
    "\n",
    "    def per_bus_wh_per_min() -> float:\n",
    "        return float(charge_rate_wh_per_hour) / 60.0\n",
    "\n",
    "    # ---------- grid limits, **per grid id** ----------\n",
    "    if power_limits_by_id is not None:\n",
    "        grid_access_norm_by_id = _build_grid_access_from_raw(power_limits_by_id)\n",
    "    else:\n",
    "        grid_access = grid_access or {}\n",
    "        grid_access_norm_by_id = {int(k): _normalize_grid_limits_start_minute(v) for k, v in grid_access.items()}\n",
    "\n",
    "    grid_occ_by_id: Dict[int, Dict[int, int]] = {}\n",
    "\n",
    "    def grid_cap_at_minute(grid_id: int, minute_idx: int) -> Optional[int]:\n",
    "        caps = grid_access_norm_by_id.get(int(grid_id), [(-1, None)])\n",
    "        t_sec = int(minute_idx) * MINUTE\n",
    "        for end_t, lim in caps:\n",
    "            if int(end_t) == -1 or t_sec <= int(end_t):\n",
    "                if lim is None:\n",
    "                    return None\n",
    "                rate = per_bus_wh_per_min()\n",
    "                if rate <= 0:\n",
    "                    return 0\n",
    "                return int(math.floor(float(lim) / rate + 1e-9))\n",
    "        return None\n",
    "\n",
    "    def grid_allows(grid_id: int, m: int) -> bool:\n",
    "        cap = grid_cap_at_minute(grid_id, m)\n",
    "        if cap == 0:\n",
    "            return False\n",
    "        occ = grid_occ_by_id.setdefault(int(grid_id), {}).get(m, 0)\n",
    "        return (cap is None) or (occ + 1 <= cap)\n",
    "\n",
    "    def add_grid_interval(grid_id: int, start_sec: int, end_sec: int) -> None:\n",
    "        if end_sec <= start_sec:\n",
    "            return\n",
    "        occ = grid_occ_by_id.setdefault(int(grid_id), {})\n",
    "        m0, m1 = start_sec // MINUTE, (end_sec - 1) // MINUTE\n",
    "        for m in range(m0, m1 + 1):\n",
    "            occ[m] = occ.get(m, 0) + 1\n",
    "\n",
    "    # ---------- station occupancy (per-minute + per-lane) ----------\n",
    "    station_occ: Dict[int, Dict[int, int]] = {}   # sid -> minute -> count\n",
    "    station_lane_occ: Dict[int, List[set]] = {}   # sid -> [set(minute)] per lane\n",
    "\n",
    "    def station_allows(sid: int, m: int, capacity: int) -> bool:\n",
    "        occ = station_occ.setdefault(int(sid), {}).get(m, 0)\n",
    "        return occ + 1 <= int(capacity)\n",
    "\n",
    "    def add_station_interval(sid: int, start_sec: int, end_sec: int) -> None:\n",
    "        if end_sec <= start_sec:\n",
    "            return\n",
    "        occ = station_occ.setdefault(int(sid), {})\n",
    "        m0, m1 = start_sec // MINUTE, (end_sec - 1) // MINUTE\n",
    "        for m in range(m0, m1 + 1):\n",
    "            occ[m] = occ.get(m, 0) + 1\n",
    "\n",
    "    def _ensure_lanes(sid: int, capacity: int) -> List[set]:\n",
    "        lanes = station_lane_occ.setdefault(int(sid), [])\n",
    "        while len(lanes) < int(capacity):\n",
    "            lanes.append(set())\n",
    "        return lanes\n",
    "\n",
    "    def _lane_is_free(lane_set: set, start_sec: int, end_sec: int) -> bool:\n",
    "        if end_sec <= start_sec:\n",
    "            return False\n",
    "        m0, m1 = start_sec // MINUTE, (end_sec - 1) // MINUTE\n",
    "        for m in range(m0, m1 + 1):\n",
    "            if m in lane_set:\n",
    "                return False\n",
    "        return True\n",
    "\n",
    "    def _reserve_lane(lane_set: set, start_sec: int, end_sec: int) -> None:\n",
    "        if end_sec <= start_sec:\n",
    "            return\n",
    "        m0, m1 = start_sec // MINUTE, (end_sec - 1) // MINUTE\n",
    "        lane_set.update(range(m0, m1 + 1))\n",
    "\n",
    "    def _assign_lane(sid: int, capacity: int, start_sec: int, end_sec: int,\n",
    "                     prefer_lane: Optional[int] = None) -> Optional[int]:\n",
    "        lanes = _ensure_lanes(sid, capacity)\n",
    "        if prefer_lane is not None and 0 <= prefer_lane < len(lanes):\n",
    "            L = lanes[prefer_lane]\n",
    "            if _lane_is_free(L, start_sec, end_sec):\n",
    "                _reserve_lane(L, start_sec, end_sec)\n",
    "                return prefer_lane\n",
    "        for idx, L in enumerate(lanes[:int(capacity)]):\n",
    "            if _lane_is_free(L, start_sec, end_sec):\n",
    "                _reserve_lane(L, start_sec, end_sec)\n",
    "                return idx\n",
    "        return None\n",
    "\n",
    "    # pad helpers\n",
    "    def _apply_grid_pad(grid_id: int, seg_start: int, seg_end: int) -> Tuple[int, int]:\n",
    "        if occupancy_pad_sec <= 0:\n",
    "            return seg_start, seg_end\n",
    "        # Left pad only if prior minute has capacity\n",
    "        occ_start = seg_start\n",
    "        if seg_start > 0:\n",
    "            m_before = (seg_start - 1) // MINUTE\n",
    "            cap_before = grid_cap_at_minute(grid_id, m_before)\n",
    "            if cap_before is None or cap_before > 0:\n",
    "                occ_start = max(0, seg_start - int(occupancy_pad_sec))\n",
    "        # Right pad only if following minute has capacity\n",
    "        occ_end = seg_end\n",
    "        m_after = seg_end // MINUTE\n",
    "        cap_after = grid_cap_at_minute(grid_id, m_after)\n",
    "        if cap_after is None or cap_after > 0:\n",
    "            occ_end = seg_end + int(occupancy_pad_sec)\n",
    "        return occ_start, occ_end\n",
    "\n",
    "    def _maybe_station_pad(seg_start: int, seg_end: int) -> Tuple[int, int]:\n",
    "        if not apply_station_pad or occupancy_pad_sec <= 0:\n",
    "            return seg_start, seg_end\n",
    "        return max(0, seg_start - int(occupancy_pad_sec)), seg_end + int(occupancy_pad_sec)\n",
    "\n",
    "    # ---------- stations grouped by depot ----------\n",
    "    stations_by_depot: Dict[int, List[Tuple[int, int, int]]] = {}\n",
    "    for sid, s in stations.items():\n",
