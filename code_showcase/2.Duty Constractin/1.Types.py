    "            if seg_minutes <= 0:\n",
    "                continue\n",
    "            seg_end_E = min(float(battery_capacity), cur_E + seg_minutes * incr_Wh_per_min)\n",
    "\n",
    "            scheduled.append({\n",
    "                \"vehicle_type\": int(vt_id),\n",
