    "        for a, b in zip(seq, seq[1:]):\n",
    "            if (a, b) in unsched_pairs:\n",
    "                new_duties.append(cur)\n",
    "                cur = [b]\n",
    "            else:\n",
    "                cur.append(b)\n",
    "        if cur:\n",
    "            new_duties.append(cur)\n",
    "    return new_duties\n",
