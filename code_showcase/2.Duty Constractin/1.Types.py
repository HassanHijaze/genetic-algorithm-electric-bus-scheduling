    "def prune_duties_by_unscheduled(duties: List[List[int]], unscheduled_events: List[dict]) -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Split duty sequences at every unscheduled merge (i->j).\n",
    "    Returns new list of duty sequences with only feasible merges left.\n",
    "    \"\"\"\n",
    "    unsched_pairs = {(e[\"i\"], e[\"j\"]) for e in unscheduled_events}\n",
    "    new_duties: List[List[int]] = []\n",
    "    for seq in duties:\n",
    "        if not seq:\n",
    "            continue\n",
