    "        dep_id, dh_id, d2 = td_best[tail]\n",
    "        return int(dep_id), int(dh_id), int(d2.get(\"duration\", 0)), float(d2.get(\"energy\", 0.0))\n",
    "    return None, None, None, None\n",
    "\n",
    "## Purpose: Look up the best trip→depot (type-2) arc for tail trip tail.\n",
    "\n",
    "def split_chain_into_energy_blocks(\n",
    "    route: List[int],\n",
    "    trips: Dict[int, dict],\n",
    "    dt_best,\n",
    "    tt_best,\n",
    "    td_best,\n",
    "    battery_capacity: float,\n",
    "    pre_depot_threshold: float = PRE_DEPOT_THRESHOLD,\n",
    "    include_type1: bool = True,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Split a time-feasible chain into energy-feasible blocks (no mid-block charge).\n",
    "    IMPORTANT: A block end is only accepted if its tail trip has a REAL type-2\n",
    "    (trip->depot) DH available (not guessed). This prevents '?->(0)' later.\n",
    "    \"\"\"\n",
    "    def _has_td(tail: int) -> bool:\n",
    "        return bool(td_best and tail in td_best)\n",
    "\n",
    "    def _td_energy(tail: int) -> float:\n",
    "        if not _has_td(tail):\n",
    "            return float(\"inf\")  # treat as impossible\n",
    "        return float(td_best[tail][2].get(\"energy\", 0.0))\n",
    "\n",
    "    blocks: List[List[int]] = []\n",
    "    n = len(route)\n",
    "    i = 0\n",
    "    while i < n:\n",
    "        E = float(battery_capacity)\n",
    "        last_good_end = None\n",
    "        j = i\n",
    "        t0 = route[j]\n",
    "\n",
    "        e_dt = 0.0\n",
    "        if include_type1 and dt_best and t0 in dt_best:\n",
    "            _, _, d = dt_best[t0]\n",
    "            e_dt = float(d.get(\"energy\", 0.0))\n",
    "\n",
    "        need_head = e_dt + float(trips[t0].get(\"energy\", 0.0))\n",
    "        if E - need_head < 0:\n",
    "            blocks.append([t0])\n",
    "            i = j + 1\n",
    "            continue\n",
    "        E -= need_head\n",
    "\n",
    "        # can we end here? (only if REAL type-2 exists)\n",
    "        if _has_td(t0):\n",
    "            e_td0 = _td_energy(t0)\n",
    "            if (E > pre_depot_threshold) and (E - e_td0 >= float(FINAL_DEPOT_RESERVE_WH)):\n",
    "                last_good_end = j\n",
    "\n",
    "        while j + 1 < n:\n",
    "            u, v = route[j], route[j + 1]\n",
    "            dh = tt_best.get((u, v))\n",
    "            e_tt = float(dh[1].get(\"energy\", 0.0)) if dh else float(\"inf\")\n",
    "            need = e_tt + float(trips[v].get(\"energy\", 0.0))\n",
    "            if E - need < 0:\n",
    "                break\n",
    "            E -= need\n",
    "\n",
    "            if _has_td(route[j + 1]):\n",
    "                e_td_tail = _td_energy(route[j + 1])\n",
    "                if (E > pre_depot_threshold) and (E - e_td_tail >= float(FINAL_DEPOT_RESERVE_WH)):\n",
    "                    last_good_end = j + 1\n",
    "            j += 1\n",
    "\n",
    "        end_idx = last_good_end if last_good_end is not None else j\n",
    "        blocks.append(route[i: end_idx + 1])\n",
    "        i = end_idx + 1\n",
    "\n",
    "    return blocks\n",
    "\n",
    "\n",
    "## Take one time-feasible chain of trips (no mid-trip timing conflicts) and cut it into energy-feasible blocks that can be driven on one battery charge without mid-block charging. A block typically:\n",
    "\n",
    "## starts at a depot with a type-1 pull-out (optional in accounting),\n",
    "\n",
    "## runs several trips with type-0 links,\n",
    "\n",
    "## and ideally can return to a depot with a type-2 pull-in while meeting safety buffers.\n",
    "\n",
    "## These blocks are later used to build duties via depot dwells (type-6 → charge → type-5).\n",
    "\n",
    "\n",
    "\n",
    "def prune_duties_by_missing_final_type2(\n",
    "    duties: List[List[int]],\n",
    "    blocks: List[List[int]],\n",
    "    td_best: Dict[int, Tuple[int, int, dict]],\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    For every duty (sequence of block indices), ensure the *tail* block's last\n",
    "    trip has a real type-2. If not, cut merges from the right until it does.\n",
    "    If none of the blocks in the duty can return, break the duty into singletons.\n",
    "    \"\"\"\n",
    "    new: List[List[int]] = []\n",
    "    for seq in duties:\n",
    "        if not seq:\n",
    "            continue\n",
    "\n",
    "        # find the rightmost block in seq that can return to a depot\n",
    "        k: Optional[int] = None\n",
    "        for idx in range(len(seq) - 1, -1, -1):\n",
    "            tail_trip = blocks[seq[idx]][-1]\n",
    "            if tail_trip in td_best:\n",
    "                k = idx\n",
    "                break\n",
    "\n",
    "        if k is None:\n",
    "            # nothing in this duty can return → keep all as singletons (no merges)\n",
    "            for bi in seq:\n",
    "                new.append([bi])\n",
    "            continue\n",
    "\n",
    "        # keep left part up to k as one duty (valid final type-2)\n",
    "        new.append(seq[:k + 1])\n",
    "\n",
    "        # the remainder (if any) becomes independent singletons; they may\n",
    "        # be re-merged later in subsequent solver passes if feasible.\n",
    "        rest = seq[k + 1:]\n",
    "        for bi in rest:\n",
    "            new.append([bi])\n",
    "\n",
    "    return new\n",
    "\n",
    "## This function is a safety fixer that runs after you’ve stitched blocks into duties.\n",
    "## Its job: guarantee every duty can finish at a depot via a real type-2.\n",
    "## If the current tail block can’t return, it chops off merges from the right until the tail can;\n",
    "## if none of the blocks in that duty can return, it splits everything into single-block duties.\n",
    "\n",
    "\n",
    "# -----------------------------------------------------------------------------\n",
    "# 5) Meta for blocks (with type-6 / type-2)\n",
    "# -----------------------------------------------------------------------------\n",
    "def _simulate_block_meta_56(\n",
    "    block: List[int],\n",
    "    trips: Dict[int, dict],\n",
    "    dt_best, tt_best, td_best,\n",
    "    d5_best, d6_best,\n",
    "    battery_capacity: float,\n",
    "    include_type1: bool = True,\n",
    ") -> dict:\n",
    "    head, tail = block[0], block[-1]\n",
    "    t_head_start = int(trips[head][\"start\"])\n",
    "    t_tail_end = int(trips[tail][\"end\"])\n",
    "\n",
    "    e_start_type1 = float(dt_best[head][2].get(\"energy\", 0.0)) if (include_type1 and head in dt_best) else 0.0\n",
    "\n",
    "    d6_tuple = d6_best.get(tail)  # (dep_id, dh_id, d)\n",
    "    td_tuple = td_best.get(tail)  # (dep_id, dh_id, d)\n",
    "    d6_dur = int(d6_tuple[2].get(\"duration\", 0)) if d6_tuple else 0\n",
    "    d6_eng = float(d6_tuple[2].get(\"energy\", 0.0)) if d6_tuple else 0.0\n",
    "    td_eng = float(td_tuple[2].get(\"energy\", 0.0)) if td_tuple else 0.0\n",
    "\n",
    "    E = float(battery_capacity) - e_start_type1 - _e_trip(trips, head)\n",
    "    for u, v in zip(block, block[1:]):\n",
    "        e_tt = (_tt_info(tt_best, u, v)[2] or 0.0)\n",
    "        E -= e_tt + _e_trip(trips, v)\n",
    "\n",
    "    arrive_mid_time = t_tail_end + d6_dur\n",
    "\n",
    "    return {\n",
    "        \"head\": head,\n",
    "        \"tail\": tail,\n",
    "        \"t_head_start\": t_head_start,\n",
    "        \"t_tail_end\": t_tail_end,\n",
    "        \"d6\": d6_tuple,\n",
    "        \"td\": td_tuple,\n",
    "        \"arrive_mid_time\": arrive_mid_time,\n",
    "        \"pre_E_full\": E,\n",
    "        \"d6_energy\": d6_eng,\n",
    "        \"td_energy\": td_eng,\n",
    "    }\n",
    "\n",
    "\n",
    "## Compute the per-block mid-day depot “pull-in” snapshot you need to decide if two blocks can be merged\n",
    "## It summarizes timing and energy at the moment block i reaches a depot (via type-6), plus the energy needed for a final end-of-day return (type-2).\n",
    "## This metadata feeds build_block_merge_graph_56.\n",
    "\n",
    "def build_blocks_and_meta_56(\n",
    "    time_only_chains: List[List[int]],\n",
    "    trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "    battery_capacity: float,\n",
    "    include_type1: bool = True,\n",
    "):\n",
    "    blocks: List[List[int]] = []\n",
    "    metas: List[dict] = []\n",
    "    for chain in time_only_chains:\n",
    "        parts = split_chain_into_energy_blocks(\n",
    "            chain, trips, dt_best, tt_best, td_best,\n",
    "            battery_capacity=battery_capacity,\n",
    "            pre_depot_threshold=PRE_DEPOT_THRESHOLD,\n",
    "            include_type1=include_type1,\n",
    "        )\n",
    "        for b in parts:\n",
    "            blocks.append(b)\n",
    "            metas.append(\n",
    "                _simulate_block_meta_56(\n",
    "                    b, trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "                    battery_capacity=battery_capacity,\n",
    "                    include_type1=include_type1,\n",
    "                )\n",
    "            )\n",
    "    return blocks, metas\n",
    "\n",
    "## Turn each time-feasible chain of trips into:\n",
    "\n",
    "## a list of energy-feasible blocks (no mid-block charging), and\n",
    "\n",
    "## a metadata record per block with the key info needed to consider mid-day depot merges (type-6 pull-in → dwell → type-5 pull-out).\n",
    "\n",
    "## This is the bridge between “pure timing” (chains) and “charging-aware stitching” (56 merges).\n",
    "\n",
    "\n",
    "# -----------------------------------------------------------------------------\n",
    "# 6) Required start energy (start=end semantics)\n",
    "# -----------------------------------------------------------------------------\n",
    "def _block_required_start_56(\n",
    "    block: List[int],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    start_kind: str,  # \"type1\" or \"type5\"\n",
    "    end_kind: str,    # \"type6\" or \"type2\"\n",
    "    dt_best, d5_best, td_best, d6_best,\n",
    "    pre_depot_threshold: float = PRE_DEPOT_THRESHOLD,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Minimum SOC (Wh) at start of block to:\n",
    "      - pay start (type-1/5) + head trip,\n",
    "      - run all TT + trips inside,\n",
    "      - pay end deadhead (type-6 mid-day or type-2 final).\n",
    "    Add MID_DEPOT_RESERVE_WH (for type-5 start) and FINAL_DEPOT_RESERVE_WH (for type-2 end).\n",
    "    \"\"\"\n",
    "    head, tail = block[0], block[-1]\n",
    "\n",
    "    # start deadhead energy\n",
    "    if start_kind == \"type1\":\n",
    "        e_start = float(dt_best[head][2].get(\"energy\", 0.0)) if head in dt_best else float(\"inf\")\n",
    "        start_reserve = 0.0\n",
    "    else:  # type-5\n",
    "        e_start = float(d5_best[head][2].get(\"energy\", 0.0)) if head in d5_best else float(\"inf\")\n",
    "        start_reserve = float(MID_DEPOT_RESERVE_WH)\n",
    "\n",
    "    total = e_start + float(trips[head].get(\"energy\", 0.0))\n",
    "    peak = total\n",
    "\n",
    "    for u, v in zip(block, block[1:]):\n",
    "        dh = tt_best.get((u, v))\n",
    "        e_tt = float(dh[1].get(\"energy\", 0.0)) if dh else 0.0\n",
    "        total += e_tt + float(trips[v].get(\"energy\", 0.0))\n",
    "        if total > peak:\n",
    "            peak = total\n",
    "\n",
    "    if end_kind == \"type6\":\n",
    "        e_end = float(d6_best[tail][2].get(\"energy\", 0.0)) if tail in d6_best else float(\"inf\")\n",
    "        need_end = e_end\n",
    "    else:  # type-2\n",
    "        e_end = float(td_best[tail][2].get(\"energy\", 0.0)) if tail in td_best else float(\"inf\")\n",
    "        need_end = max(pre_depot_threshold, e_end) + float(FINAL_DEPOT_RESERVE_WH)\n",
    "\n",
    "    required_without_reserve = max(peak, total + need_end)\n",
    "    return required_without_reserve + start_reserve\n",
    "\n",
    "\n",
    "#  A single number in Wh: the minimum SoC at the very start of the block so that you can:\n",
    "\n",
    "#  pay the start deadhead (type-1 or type-5) and the head trip,\n",
    "\n",
    "#  run all trips + TT links inside the block, and\n",
    "\n",
    "#  pay the end deadhead (type-6 if this block continues mid-day, or type-2 if this block is terminal),\n",
    "#  while also respecting the reserves:\n",
    "\n",
    "#  add MID_DEPOT_RESERVE_WH if the block starts with type-5,\n",
    "\n",
    "#  add FINAL_DEPOT_RESERVE_WH (and pre_depot_threshold) if the block ends with type2\n",
    "\n",
    "## example :start_kind=type5 → e_start=300, start_reserve=200\n",
    "## total after A = 300 + 1000 = 1300 ; peak=1300\n",
    "## add A→B + B = +150 + 1200 → total=2650 ; peak=2650\n",
    "\n",
    "## end_kind=type2 → need_end = max(100, 400) + 200=600\n",
    "\n",
    "# required_without_reserve = max(peak=2650, total+need_end=2650+600=3250) = 3250\n",
    "# return 3250 + start_reserve(200) = 3450 Wh\n",
    "\n",
    "## So you must start this block with ≥ 3450 Wh!\n",
    "\n",
    "# -----------------------------------------------------------------------------\n",
    "# 7) Merge graph using type-6 → dwell → type-5\n",
    "# -----------------------------------------------------------------------------\n",
    "def build_block_merge_graph_56(\n",
    "    blocks: List[List[int]],\n",
    "    metas: List[dict],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    dt_best: Dict[int, Tuple[int, int, dict]],\n",
    "    d5_best: Dict[int, Tuple[int, int, dict]],\n",
    "    td_best: Dict[int, Tuple[int, int, dict]],\n",
    "    d6_best: Dict[int, Tuple[int, int, dict]],\n",
    "    battery_capacity: float,\n",
    "    charge_rate_wh_per_hour: float = CHARGE_RATE_WH_PER_HOUR,\n",
    "    allow_equal_time: bool = True,\n",
    "    require_terminal_safe: bool = False,\n",
    "):\n",
    "    \"\"\"\n",
    "    Build merge candidates (i -> j) via mid-day depot dwell (type-6 → dwell → type-5).\n",
    "\n",
    "    GATING (linear model):\n",
    "      per_min = charge_rate / 60.\n",
    "      window_minutes  = floor((latest_depart_j − ceil_min(arrive_i)) / 60)\n",
    "      cap_minutes     = ceil((battery − E_arrive_i) / per_min)\n",
    "      window_cap      = min(window_minutes, cap_minutes)\n",
    "\n",
    "      need_mid_min    = ceil( max(0, req_mid   − E_arrive_i, MID_DEPOT_RESERVE_WH + e_type5 + e_head − E_arrive_i) / per_min )\n",
    "      need_term_min   = ceil( max(0, req_term  − E_arrive_i, MID_DEPOT_RESERVE_WH + e_type5 + e_head − E_arrive_i) / per_min )\n",
    "\n",
    "      Keep edge iff:\n",
    "         need_mid_min  ≤ window_cap\n",
    "      and if require_terminal_safe:\n",
    "         need_term_min ≤ window_cap\n",
    "\n",
    "    STRATEGY tweak (no rule changes):\n",
    "      After building adj/info, sort each i's adjacency by \"rarity & tightness\":\n",
    "        1) smaller in-degree of j (few alternatives to reach j) first,\n",
    "        2) tighter slack first: (dwell_avail_sec - 60*need_minutes_terminal),\n",
    "        3) smaller need_minutes_terminal first.\n",
    "    \"\"\"\n",
    "    import math\n",
    "    MINUTE = 60\n",
    "    per_min = float(charge_rate_wh_per_hour) / 60.0\n",
    "\n",
    "    def _ceil_minute(t: int) -> int:\n",
    "        return ((int(t) + MINUTE - 1) // MINUTE) * MINUTE\n",
    "\n",
    "    def _ceil_div(a: float, b: float) -> int:\n",
    "        if a <= 0:\n",
    "            return 0\n",
    "        return int(math.ceil(a / max(b, 1e-9)))\n",
    "\n",
    "    # Required start energy for each block j under two end modes\n",
    "    req_start_mid: List[float] = []\n",
    "    req_start_term: List[float] = []\n",
    "    for b in blocks:\n",
    "        req_mid = _block_required_start_56(\n",
    "            b, trips, tt_best,\n",
    "            start_kind=\"type5\", end_kind=\"type6\",\n",
    "            dt_best=dt_best, d5_best=d5_best, td_best=td_best, d6_best=d6_best,\n",
    "            pre_depot_threshold=PRE_DEPOT_THRESHOLD,\n",
    "        )\n",
    "        req_term = _block_required_start_56(\n",
    "            b, trips, tt_best,\n",
    "            start_kind=\"type5\", end_kind=\"type2\",\n",
    "            dt_best=dt_best, d5_best=d5_best, td_best=td_best, d6_best=d6_best,\n",
    "            pre_depot_threshold=PRE_DEPOT_THRESHOLD,\n",
    "        )\n",
    "        req_start_mid.append(float(req_mid))\n",
    "        req_start_term.append(float(req_term))\n",
    "\n",
    "    n = len(blocks)\n",
    "    adj: Dict[int, List[int]] = {i: [] for i in range(n)}\n",
    "    info: Dict[Tuple[int, int], dict] = {}\n",
    "\n",
    "    for i in range(n):\n",
    "        mi = metas[i]\n",
    "        if not mi.get(\"d6\"):\n",
    "            continue\n",
    "\n",
    "        tail_i   = int(mi[\"tail\"])\n",
    "        tail_end = int(mi[\"t_tail_end\"])\n",
    "        d6_map   = mi[\"d6\"]                           # (dep_id, dh_id, d)\n",
    "        d6_dur   = int(d6_map[2].get(\"duration\", 0))\n",
    "        d6_eng   = float(mi.get(\"d6_energy\", 0.0) or 0.0)\n",
    "\n",
    "        arrive_i         = tail_end + d6_dur\n",
    "        arrive_i_aligned = _ceil_minute(arrive_i)\n",
    "        E_arrive_i       = max(0.0, float(mi[\"pre_E_full\"]) - d6_eng)\n",
    "        dep_tail_i       = int(d6_map[0])\n",
    "\n",
    "        for j in range(n):\n",
    "            if i == j:\n",
    "                continue\n",
    "\n",
    "            head_j = int(metas[j][\"head\"])\n",
    "            if head_j not in d5_best:\n",
    "                continue\n",
    "\n",
    "            dep_head_j, d5_id, d5_d = d5_best[head_j]\n",
    "            dep_head_j = int(dep_head_j)\n",
    "            if dep_tail_i != dep_head_j:\n",
    "                continue\n",
    "\n",
    "            dt5_dur       = int(d5_d.get(\"duration\", 0))\n",
    "            latest_depart = int(trips[head_j][\"start\"]) - dt5_dur\n",
    "            dwell_avail   = latest_depart - arrive_i_aligned\n",
    "\n",
    "            if allow_equal_time:\n",
    "                if dwell_avail < 0:\n",
    "                    continue\n",
    "            else:\n",
    "                if dwell_avail <= 0:\n",
    "                    continue\n",
    "\n",
    "            # Best-case minutes we could take (ignore future contention)\n",
    "            window_minutes = max(0, dwell_avail // MINUTE)\n",
    "            cap_minutes = _ceil_div(max(0.0, float(battery_capacity) - E_arrive_i), per_min)\n",
    "            window_cap = min(window_minutes, cap_minutes)\n",
    "\n",
    "            req_mid  = req_start_mid[j]\n",
    "            req_term = req_start_term[j]\n",
    "            if not (math.isfinite(req_mid) and math.isfinite(req_term)):\n",
    "                continue\n",
    "            if req_mid > float(battery_capacity) + 1e-9 or req_term > float(battery_capacity) + 1e-9:\n",
    "                continue\n",
    "\n",
    "            e_type5 = float(d5_d.get(\"energy\", 0.0))\n",
    "            e_head  = float(trips[head_j].get(\"energy\", 0.0))\n",
    "            need_pos_head = MID_DEPOT_RESERVE_WH + e_type5 + e_head - E_arrive_i\n",
    "\n",
    "            need_wh_mid  = max(0.0, req_mid  - E_arrive_i, need_pos_head)\n",
    "            need_wh_term = max(0.0, req_term - E_arrive_i, need_pos_head)\n",
    "\n",
    "            need_min_mid  = max(_ceil_div(need_wh_mid,  per_min), int(MIN_CHARGE_MINUTES))\n",
    "            need_min_term = max(_ceil_div(need_wh_term, per_min), int(MIN_CHARGE_MINUTES))\n",
    "\n",
    "            # HARD GATE\n",
    "            if need_min_mid > window_cap:\n",
    "                continue\n",
    "            if require_terminal_safe and (need_min_term > window_cap):\n",
    "                continue\n",
    "\n",
    "            adj[i].append(j)\n",
    "            info[(i, j)] = {\n",
    "                \"pullin_dh_id\": int(d6_map[1]),\n",
    "                \"pullout_dh_id\": int(d5_id),\n",
    "                \"pullout_id\": int(d5_id),  # compat\n",
    "\n",
    "                \"need_minutes\": int(need_min_mid),\n",
    "                \"need_minutes_terminal\": int(need_min_term),\n",
    "\n",
    "                \"arrive_time\": int(arrive_i),\n",
    "                \"arrive_time_aligned\": int(arrive_i_aligned),\n",
    "                \"latest_depart\": int(latest_depart),\n",
    "                \"dwell_avail_sec\": int(max(0, dwell_avail)),\n",
    "\n",
    "                \"E_arrival_i\": float(E_arrive_i),\n",
    "                \"E_req_start_j\": float(req_mid),\n",
    "\n",
    "                \"window_minutes\": int(window_minutes),\n",
    "                \"cap_minutes\": int(cap_minutes),\n",
    "                \"window_cap_minutes\": int(window_cap),\n",
    "\n",
    "                \"depot_id\": int(dep_tail_i),\n",
    "            }\n",
    "\n",
    "    # --- STRATEGY: order edges by rarity & tightness (no rule change) ---\n",
    "    in_deg: Dict[int, int] = {j: 0 for j in range(n)}\n",
    "    for i in adj:\n",
    "        for j in adj[i]:\n",
    "            in_deg[j] += 1\n",
    "\n",
    "    def _edge_priority(i: int, j: int) -> Tuple[int, int, int]:\n",
    "        # smaller in-degree first (rarer target), then tighter slack first, then smaller need first\n",
    "        slack_sec = info[(i, j)][\"dwell_avail_sec\"] - 60 * info[(i, j)][\"need_minutes_terminal\"]\n",
    "        return (in_deg[j], int(slack_sec), int(info[(i, j)][\"need_minutes_terminal\"]))\n",
    "\n",
    "    for i in adj:\n",
    "        adj[i].sort(key=lambda j: _edge_priority(i, j))\n",
    "\n",
    "    return adj, info\n",
    "\n",
    "\n",
    "## Build all feasible mid-day merges between blocks:\n",
    "## block i finishes → type-6 to depot → charge dwell → type-5 out → start of block j.\n",
    "## It returns:\n",
    "\n",
    "## adj: for each block i, the list of j you can feasibly reach via a dwell.\n",
    "\n",
    "## info: per edge (i,j), the charging minutes needed, timing,\n",
    "## energy at arrival, depot id, and DH ids to execute the merge.\n",
    "\n",
    "## When we sort candidates (i → j) we use this key (ascending):\n",
    "\n",
    "## in_deg[j] — prefer rarer targets first (fewer ways to reach j).\n",
    "\n",
    "## slack_sec — prefer tighter windows first.\n",
    "\n",
    "## need_minutes_terminal — then prefer the one that needs less charging time.\n",
    "\n",
    "## So among edges that are equally scarce and similarly tight, yes—we pick the one that needs fewer minutes of charge.\n",
    "## But we first protect scarce heads and urgent windows.\n",
    "\n",
    "\n",
    "def _simulate_duty_energy(\n",
    "    seq: List[int],\n",
    "    blocks, trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "    dwell_map: Dict[Tuple[int,int], dict],\n",
    "    battery_capacity: float,\n",
    "    final_reserve_wh: float = FINAL_DEPOT_RESERVE_WH,\n",
    "):\n",
    "    \"\"\"\n",
    "    Simulate one duty's SoC (no clamping). Return (ok, failing_pair, min_soc).\n",
    "    failing_pair is an (i,j) merge to cut, or None if unfixable here.\n",
    "    \"\"\"\n",
    "    E = float(battery_capacity)\n",
    "    minE = E\n",
    "\n",
    "    def e_trip(t): return float(trips[t].get(\"energy\", 0.0))\n",
    "    def e_tt(u,v):\n",
    "        dh = tt_best.get((u,v)); return float(dh[1].get(\"energy\",0.0)) if dh else float(\"inf\")\n",
    "    def e_dt(t): x=dt_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "    def e_td(t): x=td_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "    def e_d5(t): x=d5_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "    def e_d6(t): x=d6_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "\n",
    "    # Start: type-1 + head trip of first block\n",
    "    head = blocks[seq[0]][0]\n",
    "    E -= (e_dt(head) + e_trip(head)); minE = min(minE, E)\n",
    "    if E < 0: return False, None, minE\n",
    "\n",
    "    # Walk merges\n",
    "    for a, b in zip(seq, seq[1:]):\n",
    "        # inside block a\n",
    "        ba = blocks[a]\n",
    "        for u,v in zip(ba, ba[1:]):\n",
    "            E -= (e_tt(u,v) + e_trip(v)); minE = min(minE, E)\n",
    "            if E < 0: return False, (a,b), minE  # split at this merge\n",
    "\n",
    "        # pull-in type-6\n",
    "        tail = ba[-1]\n",
    "        E -= e_d6(tail); minE = min(minE, E)\n",
    "        if E < 0: return False, (a,b), minE\n",
    "\n",
    "        # dwell energy\n",
    "        ev = dwell_map.get((a,b))\n",
    "        if not ev: return False, (a,b), minE\n",
    "        E = min(battery_capacity, float(ev.get('end_energy_float', ev['end_energy'])))\n",
    "        minE = min(minE, E)\n",
    "\n",
    "        # pull-out type-5 + head of next block\n",
    "        head_next = blocks[b][0]\n",
    "        E -= (e_d5(head_next) + e_trip(head_next)); minE = min(minE, E)\n",
    "        if E < 0: return False, (a,b), minE\n",
    "\n",
    "    # tail block internals\n",
    "    last = seq[-1]\n",
    "    bl = blocks[last]\n",
    "    for u,v in zip(bl, bl[1:]):\n",
    "        E -= (e_tt(u,v) + e_trip(v)); minE = min(minE, E)\n",
    "        if E < 0:\n",
    "            # no merge after last; best we can do is drop the last merge into this block\n",
    "            if len(seq) >= 2:\n",
    "                return False, (seq[-2], seq[-1]), minE\n",
    "            return False, None, minE\n",
    "\n",
    "    # final type-2 return; require reserve at arrival\n",
    "    tail = bl[-1]\n",
    "    E -= e_td(tail); minE = min(minE, E)\n",
    "    if E < final_reserve_wh:\n",
    "        if len(seq) >= 2:\n",
    "            return False, (seq[-2], seq[-1]), minE\n",
    "        return False, None, minE\n",
    "\n",
    "    return True, None, minE\n",
    "\n",
    "## Given one duty (a sequence of block indices) and the placed dwell(s) between blocks,\n",
    "## it simulates the bus state of charge (SoC) from a full battery, step by step, and tells you:\n",
    "\n",
    "## is the whole duty energy-feasible?\n",
    "\n",
    "## if not, which merge (a → b) is the first one to cut to fix it\n",
    "\n",
    "## the lowest SoC seen along the way (for diagnostics)\n",
    "\n",
    "\n",
    "def prune_energy_infeasible_merges(\n",
    "    duties: List[List[int]],\n",
    "    blocks, trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "    scheduled_events: List[dict],\n",
    "    battery_capacity: float,\n",
    ") -> Set[Tuple[int,int]]:\n",
    "    dwell_map = {(e['i'], e['j']): e for e in scheduled_events}\n",
    "    bad: Set[Tuple[int,int]] = set()\n",
    "    for seq in duties:\n",
    "        ok, pair, _ = _simulate_duty_energy(\n",
    "            seq, blocks, trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "            dwell_map, battery_capacity\n",
    "        )\n",
    "        if not ok and pair:\n",
    "            bad.add(pair)\n",
    "    return bad\n",
    "\n",
