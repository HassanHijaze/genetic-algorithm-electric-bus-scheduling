    "                continue\n",
    "            dep_id = int(inf[\"depot_id\"])\n",
    "            arrive = int(inf.get(\"arrive_time_aligned\", inf[\"arrive_time\"]))\n",
    "            latest = int(inf[\"latest_depart\"])\n",
    "            E_arr = float(inf.get(\"E_arrival_i\", 0.0))\n",
    "            need_min_mid  = int(max(0, inf.get(\"need_minutes\", 0)))\n",
    "            need_min_term = int(max(0, inf.get(\"need_minutes_terminal\", need_min_mid)))\n",
