    "# 7b) Stitch blocks by matching\n",
    "# -----------------------------------------------------------------------------\n",
    "def stitch_blocks_by_matching(\n",
    "    adj: Dict[int, List[int]],\n",
    "    blocks: List[List[int]],\n",
    "    trips: Dict[int, dict],\n",
    "):\n",
    "    \"\"\"\n",
    "    Stitch energy-feasible blocks into duties via maximum matching.\n",
    "    Randomness preserved as in original.\n",
    "    \"\"\"\n",
    "    import time as _time\n",
    "    _local_rng = globals().get(\"_RNG\", random.Random(globals().get(\"RAND_SEED\", None) or _time.time_ns()))\n",
    "    RANDOMIZE = globals().get(\"RANDOMIZE_BLOCK_MATCH\", True)\n",
    "    JIT = globals().get(\"RANDOM_SORT_JITTER\", 1e-3)\n",
    "\n",
    "    def _jit() -> float:\n",
    "        return (_local_rng.random() - 0.5) * 2.0 * JIT\n",
    "\n",
    "    n = len(blocks)\n",
    "\n",
    "    if RANDOMIZE:\n",
    "        for i in adj:\n",
    "            _local_rng.shuffle(adj[i])\n",
    "\n",
    "    U = list(range(n))\n",
    "    V = list(range(n))\n",
    "    if RANDOMIZE:\n",
    "        _local_rng.shuffle(U)\n",
    "        _local_rng.shuffle(V)\n",
    "\n",
    "    pairU, _ = _hk_max_matching(U, V, adj)\n",
    "    succ = {u: pairU[u] for u in U if pairU[u] is not None}\n",
    "    pred = {pairU[u]: u for u in U if pairU[u] is not None}\n",
    "\n",
    "    heads = [i for i in range(n) if i not in pred]\n",
    "    heads.sort(key=lambda i: (int(trips[blocks[i][0]][\"start\"]) + _jit()))\n",
    "\n",
    "    duties: List[List[int]] = []\n",
    "    for h in heads:\n",
    "        seq = [h]\n",
    "        cur = h\n",
    "        while cur in succ:\n",
    "            cur = succ[cur]\n",
    "            seq.append(cur)\n",
    "        duties.append(seq)\n",
    "\n",
    "    covered = {i for d in duties for i in d}\n",
    "    for i in range(n):\n",
    "        if i not in covered:\n",
    "            duties.append([i])\n",
    "\n",
    "    return duties\n",
    "\n",
    "# -----------------------------------------------------------------------------\n",
