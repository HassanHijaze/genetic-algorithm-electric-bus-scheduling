    "from typing import Dict, List, Tuple, Set\n",
    "import random, time as _time\n",
    "\n",
    "def smart_chromosome_A(\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    dt_best, td_best,\n",
    "    battery_capacity: float,\n",
    "    alpha: float = 1.0,        # weight for TT duration (min)\n",
    "    beta: float = 1e-4,        # weight for TT energy (Wh)\n",
    "    gamma: float = 0.10,       # weight for idle gap (min)\n",
    "    depot_mismatch_pen: float = 2.0,  # penalty (min) if head’s natural depot changes\n",
    "    max_idle_gap_sec: int = 1800,     # skip very long waits inside a chain\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Build a 'smart' chromosome = list of time-feasible routes (trip IDs).\n",
    "    Deterministic greedy by default; can add controlled diversity with RANDOMIZE_INIT.\n",
    "    \"\"\"\n",
    "\n",
    "    # --- local RNG & knobs (read from globals so you can tweak centrally) ---\n",
    "    _local_rng = globals().get(\"_RNG\", random.Random(globals().get(\"RAND_SEED\", None) or _time.time_ns()))\n",
    "    _INIT_TOP_K   = max(1, int(globals().get(\"INIT_TOP_K\", 3)))\n",
    "    _INIT_JITTER  = float(globals().get(\"INIT_JITTER\", 0.0))\n",
    "    _RND_INIT     = bool(globals().get(\"RANDOMIZE_INIT\", False))\n",
    "    _EPS          = float(globals().get(\"INIT_EPSILON\", 0.0))\n",
    "\n",
    "    def _jit() -> float:\n",
    "        return ((_local_rng.random() - 0.5) * 2.0 * _INIT_JITTER) if _INIT_JITTER > 0 else 0.0\n",
    "\n",
    "    # Order trips by start time; when randomized, softly shuffle ties\n",
    "    if _RND_INIT:\n",
    "        remaining = sorted(trips.keys(), key=lambda t: (int(trips[t][\"start\"]), _local_rng.random()))\n",
    "    else:\n",
    "        remaining = sorted(trips.keys(), key=lambda t: int(trips[t][\"start\"]))\n",
    "\n",
    "    remaining_set: Set[int] = set(remaining)\n",
    "    routes: List[List[int]] = []\n",
    "\n",
    "    while remaining:\n",
    "        # Pick route seed\n",
    "        if _RND_INIT and len(remaining) >= 2:\n",
    "            # look at the earliest few; pick one randomly (keeps chronology sane)\n",
    "            window = 3 if len(remaining) >= 6 else 1\n",
    "            idx = _local_rng.randrange(min(window, len(remaining)))\n",
    "            seed = remaining.pop(idx)\n",
    "        else:\n",
    "            seed = remaining.pop(0)\n",
    "\n",
    "        if seed not in remaining_set:\n",
    "            continue\n",
    "        remaining_set.remove(seed)\n",
    "\n",
    "        r_depot = _natural_depot_for_trip(seed, dt_best, td_best, default_depot=0)\n",
    "        route = [seed]\n",
    "\n",
    "        E = float(battery_capacity)\n",
    "        e_type1 = float(dt_best[seed][2].get(\"energy\", 0.0)) if seed in dt_best else 0.0\n",
    "        E -= e_type1 + _trip_energy(trips, seed)\n",
    "\n",
    "        last = seed\n",
    "        while True:\n",
    "            candidates: List[Tuple[float, int, float]] = []  # (cost, v, e_tt)\n",
    "            end_last = int(trips[last][\"end\"])\n",
    "\n",
    "            for v in list(remaining_set):\n",
    "                if not _is_tt_feasible(last, v, trips, tt_best, allow_equal=ALLOW_EQUAL_TIME):\n",
    "                    continue\n",
    "                e_tt, tt_dur = _tt_energy_duration(tt_best, last, v)\n",
    "                if not (e_tt < float(\"inf\")):\n",
    "                    continue\n",
    "                idle = max(0, int(trips[v][\"start\"]) - (end_last + tt_dur))\n",
    "                if idle > max_idle_gap_sec:\n",
    "                    continue\n",
    "\n",
    "                cost = (alpha * (tt_dur / 60.0) + beta * e_tt + gamma * (idle / 60.0))\n",
    "                v_dep = _natural_depot_for_trip(v, dt_best, td_best, default_depot=r_depot)\n",
    "                if v_dep != r_depot:\n",
    "                    cost += depot_mismatch_pen\n",
    "\n",
    "                candidates.append((cost + _jit(), v, e_tt))  # jitter for stable tie-breaks\n",
    "\n",
    "            if not candidates:\n",
    "                break\n",
    "\n",
    "            # sort by (slightly jittered) cost\n",
    "            candidates.sort(key=lambda x: x[0])\n",
    "\n",
    "            # energy guard: ensure we can still make a safe final type-2 from v\n",
    "            feasible: List[Tuple[float, int, float]] = []\n",
    "            for cst, v, e_tt in candidates:\n",
    "                E_next = E - (e_tt + _trip_energy(trips, v))\n",
    "                e_td_v = float(td_best[v][2].get(\"energy\", 0.0)) if v in td_best else 0.0\n",
    "                if E_next - e_td_v >= float(FINAL_DEPOT_RESERVE_WH):\n",
    "                    feasible.append((cst, v, e_tt))\n",
    "\n",
    "            if not feasible:\n",
    "                break\n",
    "\n",
    "            # pick the next trip\n",
    "            if _RND_INIT and (_local_rng.random() < _EPS):\n",
    "                # ε-greedy exploration: any feasible\n",
    "                _, v, e_tt = _local_rng.choice(feasible)\n",
    "            elif _RND_INIT:\n",
    "                # choose among the best-K feasible\n",
    "                k = min(_INIT_TOP_K, len(feasible))\n",
    "                _, v, e_tt = _local_rng.choice(feasible[:k])\n",
    "            else:\n",
    "                # deterministic best\n",
    "                _, v, e_tt = feasible[0]\n",
    "\n",
    "            route.append(v)\n",
    "            remaining_set.remove(v)\n",
    "            try:\n",
    "                remaining.remove(v)\n",
    "            except ValueError:\n",
    "                pass\n",
    "\n",
    "            E -= (e_tt + _trip_energy(trips, v))\n",
    "            last = v\n",
    "\n",
    "        routes.append(route)\n",
    "\n",
    "    # Repair to time-feasible chains (usually already fine)\n",
    "    repaired: List[List[int]] = []\n",
    "    for r in routes:\n",
    "        repaired.extend(_repair_route_into_chains(r, trips, tt_best, allow_equal=ALLOW_EQUAL_TIME))\n",
    "\n",
    "    repaired.sort(key=lambda c: int(trips[c[0]][\"start\"]))\n",
    "    return repaired\n",
    "\n",
    "# ---------- Decoder ----------\n",
    "def _merge_chains_min_path_cover(\n",
    "    chains: List[List[int]],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    allow_equal: bool = True,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Given pre-repaired chains (each time-feasible internally), connect chain tails to\n",
    "    other chain heads wherever a type-0 (trip->trip) is time-feasible. Then compute a\n",
    "    maximum matching to minimize the number of chains (minimum path cover on this chain-DAG).\n",
    "\n",
    "    Notes:\n",
    "      • Internal trip order inside each input chain is preserved.\n",
    "      • Only tail(i) -> head(j) links are considered (no reordering inside chains).\n",
    "      • Uses your _hk_max_matching() and _is_tt_feasible().\n",
    "    \"\"\"\n",
    "    # Filter empties defensively\n",
    "    chains = [c for c in chains if c]\n",
    "    n = len(chains)\n",
    "    if n <= 1:\n",
    "        return chains\n",
    "\n",
    "    # Build adjacency between chains (i -> j if tail(i) -> head(j) is time-feasible)\n",
    "    adj: Dict[int, List[int]] = {i: [] for i in range(n)}\n",
    "    for i in range(n):\n",
    "        tail = chains[i][-1]\n",
    "        for j in range(n):\n",
    "            if i == j:\n",
    "                continue\n",
    "            head = chains[j][0]\n",
    "            if _is_tt_feasible(tail, head, trips, tt_best, allow_equal=allow_equal):\n",
    "                adj[i].append(j)\n",
    "\n",
    "    U = list(range(n))\n",
    "    V = list(range(n))\n",
    "    pairU, _ = _hk_max_matching(U, V, adj)\n",
    "\n",
    "    succ = {u: pairU[u] for u in U if pairU[u] is not None}\n",
    "    pred = {pairU[u]: u for u in U if pairU[u] is not None}\n",
    "\n",
    "    # Chain heads = nodes without predecessors; order by time for stability\n",
    "    heads = [i for i in range(n) if i not in pred]\n",
    "    heads.sort(key=lambda i: int(trips[chains[i][0]][\"start\"]))\n",
    "\n",
    "    merged: List[List[int]] = []\n",
    "    for h in heads:\n",
    "        route = list(chains[h])\n",
    "        cur = h\n",
    "        while cur in succ:\n",
    "            cur = succ[cur]\n",
    "            route.extend(chains[cur])\n",
    "        merged.append(route)\n",
    "\n",
    "    # In a DAG, all nodes are covered via heads traversal. Still, be defensive:\n",
    "    covered = set()\n",
    "    for r in merged:\n",
    "        # mark indices by walking again (optional, no-op here)\n",
    "        pass\n",
    "\n",
    "    return merged\n",
    "\n",
    "\n",
    "# ================================\n",
    "# Intelligent, depot-agnostic decoder (drop-in)\n",
    "# Goal: keep MIN path-cover cardinality but bias WHICH chains are merged\n",
    "# toward edges that are easier to make energy-feasible later\n",
    "# (short idle windows, short TT, low energy jump, smoothness).\n",
    "# Signature unchanged so it plugs into your ctx[\"decode_chromosome\"].\n",
    "# ================================\n",
    "\n",
    "def decode_chromosome(\n",
    "    chrom: List[List[int]],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    allow_equal: bool = ALLOW_EQUAL_TIME,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Chromosome -> list of time-feasible chains with a PRIORITY chain-merge pass:\n",
    "      1) Clean each route (de-dup, drop unknowns) and split where TT is infeasible.\n",
    "      2) Add any missing trips as singletons.\n",
    "      3) Merge chains via a *priority* minimum path cover on the chain-DAG (tail->head),\n",
    "         still achieving minimum chain count, but choosing the matching that favors:\n",
    "            • small idle gaps before the next head,\n",
    "            • short TT duration,\n",
    "            • low added energy at the jump (TT + head trip),\n",
    "            • smooth energy-density transitions (|Δ Wh/min| small),\n",
    "            • protecting “critical” heads (smallest feasible idle),\n",
    "            • keeping paths that remain good one step ahead (continuation look-ahead).\n",
    "    \"\"\"\n",
    "    # ---------- 1) repair routes into time-feasible chains ----------\n",
    "    chains: List[List[int]] = []\n",
    "    used: Set[int] = set()\n",
    "\n",
    "    for route in chrom:\n",
    "        parts = _repair_route_into_chains(route, trips, tt_best, allow_equal)\n",
    "        for c in parts:\n",
    "            if c:\n",
    "                chains.append(c)\n",
    "                used.update(c)\n",
    "\n",
    "    # ---------- 2) add missing trips as singletons ----------\n",
    "    for t in trips:\n",
    "        if t not in used:\n",
    "            chains.append([t])\n",
    "\n",
    "    if not chains:\n",
    "        return []\n",
    "\n",
    "    # Stable pre-merge order (by head start) for determinism\n",
    "    chains.sort(key=lambda c: int(trips[c[0]][\"start\"]))\n",
    "\n",
    "    # ---------- 3) priority minimum path cover on chain-DAG ----------\n",
    "    chains = _merge_chains_min_path_cover_priority(\n",
    "        chains, trips, tt_best, allow_equal=allow_equal\n",
    "    )\n",
    "\n",
    "    # Final stable sort for printing\n",
    "    chains.sort(key=lambda c: int(trips[c[0]][\"start\"]))\n",
    "    return chains\n",
    "\n",
    "\n",
    "def _merge_chains_min_path_cover_priority(\n",
    "    chains: List[List[int]],\n",
    "    trips: Dict[int, dict],\n",
    "    tt_best: Dict[Tuple[int, int], Tuple[int, dict]],\n",
    "    allow_equal: bool = True,\n",
    "    *,\n",
    "    # Weights for the edge-penalty model (tune gently to your data)\n",
    "    w_idle: float   = 1.2,      # penalty per idle minute (arrive->head start)\n",
    "    w_dur: float    = 0.6,      # penalty per TT minute\n",
    "    w_energy: float = 0.0015,   # penalty per Wh of (TT energy + head trip energy)\n",
    "    w_density: float = 0.05,    # penalty per |Δ energy density| in Wh/min\n",
    "    w_ratio: float  = 0.2,      # penalty per (idle_minutes / max(1, head_minutes))\n",
    "    w_headcrit: float = 0.8,    # extra penalty for edges far from head’s tightest feasible idle\n",
    "    w_cont: float   = 0.3,      # continuation look-ahead weight: add best penalty of j->k\n",
    "    random_jitter: float = 1e-6,\n",
    ") -> List[List[int]]:\n",
    "    \"\"\"\n",
    "    Build chain DAG (i -> j if tail(i) can reach head(j) via type-0 in time).\n",
    "    Compute a maximum-cardinality matching but visit neighbors in an order that\n",
    "    minimizes a composite edge penalty:\n",
    "      idle/time + energy jump + density smoothness + head criticality + continuation.\n",
    "    \"\"\"\n",
    "    # ---- small helpers (trip/tt energy & timing) ----\n",
    "    def _tt_metrics(u: int, v: int) -> Tuple[int, float]:\n",
    "        dh = tt_best.get((u, v))\n",
    "        if not dh:\n",
    "            return (10**9, float(\"inf\"))\n",
    "        d = dh[1]\n",
    "        return int(d.get(\"duration\", 0)), float(d.get(\"energy\", 0.0))\n",
    "\n",
    "    def _feasible_tail_to_head(u_tail: int, v_head: int) -> Tuple[bool, int, int]:\n",
    "        tt_dur, _ = _tt_metrics(u_tail, v_head)\n",
    "        arrive = int(trips[u_tail][\"end\"]) + tt_dur\n",
    "        start_v = int(trips[v_head][\"start\"])\n",
    "        ok = (arrive <= start_v) if allow_equal else (arrive < start_v)\n",
    "        idle = 0 if not ok else max(0, start_v - arrive)\n",
    "        return ok, idle, tt_dur\n",
    "\n",
    "    def _trip_energy(t: int) -> float:\n",
    "        try:\n",
    "            return float(trips[t].get(\"energy\", 0.0))\n",
    "        except Exception:\n",
    "            return 0.0\n",
    "\n",
    "    def _trip_minutes(t: int) -> int:\n",
    "        try:\n",
    "            return max(1, (int(trips[t][\"end\"]) - int(trips[t][\"start\"])) // 60)\n",
    "        except Exception:\n",
    "            return 1\n",
    "\n",
    "    def _energy_density_wh_per_min(t: int) -> float:\n",
    "        return _trip_energy(t) / float(_trip_minutes(t))\n",
    "\n",
    "    n = len(chains)\n",
    "    if n <= 1:\n",
    "        return chains\n",
    "\n",
    "    # ---- build adjacency and raw features ----\n",
    "    adj: Dict[int, List[int]] = {i: [] for i in range(n)}\n",
    "    idle_map: Dict[Tuple[int, int], int] = {}\n",
    "    tt_dur_map: Dict[Tuple[int, int], int] = {}\n",
    "    e_tt_map: Dict[Tuple[int, int], float] = {}\n",
    "    e_head_map: Dict[Tuple[int, int], float] = {}\n",
    "    dens_delta_map: Dict[Tuple[int, int], float] = {}\n",
    "    head_min_idle: Dict[int, int] = {j: 10**12 for j in range(n)}  # criticality baseline\n",
    "\n",
    "    for i in range(n):\n",
    "        u_tail = chains[i][-1]\n",
    "        dens_tail = _energy_density_wh_per_min(u_tail)\n",
    "        for j in range(n):\n",
    "            if i == j:\n",
    "                continue\n",
    "            v_head = chains[j][0]\n",
    "            ok, idle, tt_dur = _feasible_tail_to_head(u_tail, v_head)\n",
    "            if not ok:\n",
    "                continue\n",
    "            dur, e_tt = _tt_metrics(u_tail, v_head)\n",
    "            e_head = _trip_energy(v_head)\n",
    "            dens_head = _energy_density_wh_per_min(v_head)\n",
    "            dens_delta = abs(dens_head - dens_tail)\n",
    "\n",
    "            adj[i].append(j)\n",
    "            idle_map[(i, j)] = idle\n",
    "            tt_dur_map[(i, j)] = dur\n",
    "            e_tt_map[(i, j)] = e_tt\n",
    "            e_head_map[(i, j)] = e_head\n",
    "            dens_delta_map[(i, j)] = dens_delta\n",
    "            # update head criticality (tightest idle)\n",
    "            if idle < head_min_idle[j]:\n",
    "                head_min_idle[j] = idle\n",
    "\n",
    "    # If no edges, every chain is already a duty\n",
    "    if all(len(adj[i]) == 0 for i in range(n)):\n",
    "        return chains\n",
    "\n",
    "    # ---- degree stats ----\n",
    "    in_deg: Dict[int, int] = {j: 0 for j in range(n)}\n",
    "    out_deg: Dict[int, int] = {i: len(adj[i]) for i in range(n)}\n",
    "    for i in range(n):\n",
    "        for j in adj[i]:\n",
    "            in_deg[j] += 1\n",
    "\n",
    "    # ---- continuation look-ahead: best penalty from each head j to any k ----\n",
    "    # We compute a simplified penalty for j->k (same formula, but it only depends on features we already have).\n",
    "    # To avoid circular dependency, compute with the same head_min_idle[] (already known).\n",
    "    def _edge_penalty_core(i: int, j: int) -> float:\n",
    "        idle = idle_map[(i, j)]\n",
    "        dur  = tt_dur_map[(i, j)]\n",
    "        e_tt = e_tt_map[(i, j)]\n",
    "        e_h  = e_head_map[(i, j)]\n",
    "        dens_delta = dens_delta_map[(i, j)]\n",
    "        # head and trip minutes for ratio term\n",
    "        head_trip_min = _trip_minutes(chains[j][0])\n",
    "        ratio = (idle // 60) / max(1, head_trip_min)  # unitless\n",
    "        pen = (\n",
    "            w_idle   * (idle / 60.0)\n",
    "            + w_dur  * (dur  / 60.0)\n",
    "            + w_energy * (e_tt + e_h)\n",
    "            + w_density * dens_delta\n",
    "            + w_ratio * ratio\n",
    "        )\n",
    "        # how far this edge is from the head's tightest feasible idle (minutes)\n",
    "        crit_gap_min = max(0, idle - head_min_idle[j]) / 60.0\n",
    "        pen += w_headcrit * crit_gap_min\n",
    "        return pen\n",
    "\n",
    "    best_cont_penalty_for_head: Dict[int, float] = {}\n",
    "    for j in range(n):\n",
    "        best = None\n",
    "        if j in adj:  # j as a tail\n",
    "            for k in adj[j]:\n",
    "                p = _edge_penalty_core(j, k)\n",
    "                if (best is None) or (p < best):\n",
    "                    best = p\n",
    "        best_cont_penalty_for_head[j] = 0.0 if best is None else float(best)\n",
    "\n",
    "    # ---- neighbor ordering by composite penalty + continuation ----\n",
    "    rng = globals().get(\"_RNG\", None)\n",
    "    jitter_scale = float(globals().get(\"RANDOM_SORT_JITTER\", random_jitter))\n",
    "    def _jitter() -> float:\n",
    "        if jitter_scale <= 0:\n",
    "            return 0.0\n",
    "        if rng is not None:\n",
    "            return (rng.random() - 0.5) * 2.0 * jitter_scale\n",
    "        import random as _r\n",
    "        return (_r.random() - 0.5) * 2.0 * jitter_scale\n",
    "\n",
    "    def _edge_penalty(i: int, j: int) -> float:\n",
    "        base = _edge_penalty_core(i, j)\n",
    "        cont = best_cont_penalty_for_head.get(j, 0.0)\n",
    "        return base + w_cont * cont\n",
    "\n",
    "    for i in range(n):\n",
    "        adj[i].sort(\n",
    "            key=lambda j: (\n",
    "                _edge_penalty(i, j) + _jitter(),     # lowest composite penalty first\n",
    "                in_deg[j],                           # rarer targets first\n",
    "                int(trips[chains[j][0]][\"start\"]),   # earlier heads first\n",
    "            )\n",
    "        )\n",
    "\n",
    "    # ---- maximum-cardinality matching with priority neighbor order (Kuhn DFS) ----\n",
    "    pairU: Dict[int, Optional[int]] = {i: None for i in range(n)}\n",
    "    pairV: Dict[int, Optional[int]] = {j: None for j in range(n)}\n",
    "\n",
    "    # Process scarce sources first: low out-degree, then earlier start\n",
    "    U_order = sorted(range(n), key=lambda i: (out_deg[i], int(trips[chains[i][0]][\"start\"])))\n",
    "\n",
    "    def _try_dfs(i: int, seenV: Set[int]) -> bool:\n",
    "        for j in adj[i]:\n",
    "            if j in seenV:\n",
    "                continue\n",
    "            seenV.add(j)\n",
    "            if pairV[j] is None or _try_dfs(pairV[j], seenV):\n",
    "                pairU[i] = j\n",
    "                pairV[j] = i\n",
    "                return True\n",
    "        return False\n",
    "\n",
    "    for i in U_order:\n",
    "        if pairU[i] is None:\n",
    "            _try_dfs(i, set())\n",
    "\n",
    "    # ---- unwind matching into disjoint paths (minimum path cover) ----\n",
    "    succ = {i: pairU[i] for i in range(n) if pairU[i] is not None}\n",
    "    pred = {pairU[i]: i for i in range(n) if pairU[i] is not None}\n",
    "\n",
    "    heads = [i for i in range(n) if i not in pred]\n",
    "    heads.sort(key=lambda i: int(trips[chains[i][0]][\"start\"]))\n",
    "\n",
    "    merged: List[List[int]] = []\n",
    "    for h in heads:\n",
    "        route = list(chains[h])\n",
    "        cur = h\n",
    "        while cur in succ:\n",
    "            cur = succ[cur]\n",
    "            route.extend(chains[cur])\n",
    "        merged.append(route)\n",
    "\n",
    "    return merged\n",
