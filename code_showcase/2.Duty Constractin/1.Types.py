    "def build_routes_text_lines_56_with_capacity(\n",
    "    duties,\n",
    "    blocks,\n",
    "    trips,\n",
    "    deadheads,\n",
    "    dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "    stations,\n",
    "    scheduled_events,\n",
    "    vehicle_type_id,\n",
    "    battery_capacity,\n",
    "    pre_depot_threshold=PRE_DEPOT_THRESHOLD,\n",
    "    dwell_id_map=None,\n",
    "    merge_info: Dict[Tuple[int, int], dict] | None = None,\n",
    ") -> List[str]:\n",
    "    \"\"\"\n",
    "    Routes printer (type-1 → trips/TT → type-6 → dwell → type-5 … → type-2),\n",
    "    with **no '?' tokens**. Any missing DH segment is simply omitted from the text.\n",
    "    \"\"\"\n",
    "\n",
    "    def Eint(E: float) -> int: return int(round(E))\n",
    "    def e_trip(t: int) -> float: return float(trips[t].get(\"energy\", 0.0))\n",
    "\n",
    "    def dt_entry(t: int):\n",
    "        return dt_best.get(t)  # (dep_id, dh_id, d) or None\n",
    "\n",
    "    def tt_entry(u: int, v: int):\n",
    "        return tt_best.get((u, v))  # (dh_id, d) or None\n",
    "\n",
    "    def td_entry(t: int):\n",
    "        return td_best.get(t)  # (dep_id, dh_id, d) or None\n",
    "\n",
    "    def d5_entry(t: int):\n",
    "        return d5_best.get(t)  # (dep_id, dh_id, d) or None\n",
    "\n",
    "    def d6_entry(t: int):\n",
    "        return d6_best.get(t)  # (dep_id, dh_id, d) or None\n",
    "\n",
    "    # map (i,j) -> scheduled event\n",
    "    sched_map = {(e[\"i\"], e[\"j\"]): e for e in scheduled_events}\n",
    "\n",
    "    # optional: minutes needed per merge (only for deciding whether to say [UNSCHEDULED] — we won’t print '?')\n",
    "    need_min_map: Dict[Tuple[int, int], int] = {}\n",
    "    if merge_info is not None:\n",
    "        for (i, j), inf in merge_info.items():\n",
    "            need_min_map[(i, j)] = int(max(0, inf.get(\"need_minutes\", 0)))\n",
    "\n",
    "    lines: List[str] = []\n",
    "\n",
    "    for duty_idx, seq in enumerate(duties):\n",
    "        if not seq:\n",
    "            continue\n",
    "\n",
    "        first_block = blocks[seq[0]]\n",
    "        head0 = first_block[0]\n",
    "        dep_header = 0\n",
    "        dt0 = dt_entry(head0)\n",
    "        if dt0:\n",
    "            dep_header = int(dt0[0])\n",
    "        else:\n",
    "            # fallback: try natural depot from td if present\n",
    "            td0 = td_entry(head0)\n",
    "            if td0:\n",
    "                dep_header = int(td0[0])\n",
    "\n",
    "        E = float(battery_capacity)\n",
    "        parts = [f\"{duty_idx}: <{vehicle_type_id}> ({dep_header}) {{{Eint(E)}}}\"]\n",
    "\n",
    "        for pos, bi in enumerate(seq):\n",
    "            block = blocks[bi]\n",
    "            head, tail = block[0], block[-1]\n",
    "\n",
    "            # start of this block\n",
    "            if pos == 0:\n",
    "                dt0 = dt_entry(head)\n",
    "                if dt0:\n",
    "                    dep_id, dh_id, d = dt0\n",
    "                    e_in = float(d.get(\"energy\", 0.0))\n",
    "                    E = max(0.0, E - (e_in + e_trip(head)))\n",
    "                    parts.append(f\"--{int(dh_id)}-> {head} {{{Eint(E)}}}\")\n",
    "                else:\n",
    "                    # no type-1 known: just show the trip (no '?')\n",
    "                    E = max(0.0, E - e_trip(head))\n",
    "                    parts.append(f\"{head} {{{Eint(E)}}}\")\n",
    "            else:\n",
    "                d5 = d5_entry(head)\n",
    "                if d5:\n",
    "                    _, dh_id, d = d5\n",
    "                    e_in = float(d.get(\"energy\", 0.0))\n",
    "                    E = max(0.0, E - (e_in + e_trip(head)))\n",
    "                    parts.append(f\"--{int(dh_id)}-> {head} {{{Eint(E)}}}\")\n",
    "                else:\n",
    "                    # safety: if somehow no type-5, just show the trip without DH id\n",
    "                    E = max(0.0, E - e_trip(head))\n",
    "                    parts.append(f\"{head} {{{Eint(E)}}}\")\n",
    "\n",
    "            # inside block (trip->trip)\n",
    "            for u, v in zip(block, block[1:]):\n",
    "                tte = tt_entry(u, v)\n",
    "                if not tte:\n",
    "                    # skip unknown TT; no '?'\n",
    "                    E = max(0.0, E - e_trip(v))\n",
    "                    parts.append(f\"{v} {{{Eint(E)}}}\")\n",
    "                    continue\n",
    "                dh_id, d = tte\n",
    "                e_tt = float(d.get(\"energy\", 0.0))\n",
    "                E = max(0.0, E - (e_tt + e_trip(v)))\n",
    "                parts.append(f\"--{int(dh_id)}-> {v} {{{Eint(E)}}}\")\n",
    "\n",
    "            # between blocks: type-6, then dwell, then type-5 onto next head\n",
    "            if pos < len(seq) - 1:\n",
    "                d6 = d6_entry(tail)\n",
    "                next_bi = seq[pos + 1]\n",
    "                key = (bi, next_bi)\n",
    "\n",
    "                if d6:\n",
    "                    dep_out, d6_id, d = d6\n",
    "                    e6 = float(d.get(\"energy\", 0.0))\n",
    "                    E = max(0.0, E - e6)\n",
    "                    parts.append(f\"--{int(d6_id)}-> [{int(dep_out)}] {{{Eint(E)}}}\")\n",
    "                # else: if no type-6 (shouldn't happen after gating), skip silently.\n",
    "\n",
    "                ev = sched_map.get(key)\n",
    "                if ev is not None:\n",
    "                    # align printed start-of-dwell SoC with the SoC we had locally\n",
    "                    ev[\"start_energy_route_float\"] = float(E)\n",
    "                    ev[\"start_energy_route\"] = int(round(E))\n",
    "                    E = float(ev.get(\"end_energy_float\", ev[\"end_energy\"]))\n",
    "                    dwell_token = dwell_id_map.get(key, \"?\") if dwell_id_map else \"?\"\n",
    "                    # We still print a token, but never a bare '?': use the numeric event_id if present\n",
    "                    if dwell_token == \"?\":\n",
    "                        parts.append(f\"~{ev['event_id']}~ {{{Eint(E)}}}\")\n",
    "                    else:\n",
    "                        parts.append(f\"~{dwell_token}~ {{{Eint(E)}}}\")\n",
    "                else:\n",
    "                    # Merge shouldn’t remain after pruning; silently omit if any slipped through.\n",
    "                    pass\n",
    "\n",
    "        # final return (type-2) — print only if it really exists\n",
    "        last_tail = blocks[seq[-1]][-1]\n",
    "        td0 = td_entry(last_tail)\n",
    "        if td0:\n",
    "            dep_final, td_id, d = td0\n",
    "            e2 = float(d.get(\"energy\", 0.0))\n",
    "            E = max(0.0, E - e2)\n",
    "            parts.append(f\"--{int(td_id)}-> ({int(dep_final)}) {{{Eint(E)}}}\")\n",
    "        # else: no print; no '?'\n",
    "\n",
    "        lines.append(\" \".join(parts))\n",
    "\n",
    "    return lines\n",
