    "                \"start_energy_float\": float(cur_E),\n",
    "                \"end_energy\": int(round(seg_end_E)),\n",
    "                \"end_energy_float\": float(seg_end_E),\n",
    "                \"credited_minutes\": int(seg_minutes),\n",
    "                \"requested_minutes\": int(ev[\"need_minutes\"]),\n",
    "                \"duty_idx\": ev[\"duty_idx\"],\n",
    "                \"i\": ev[\"i\"], \"j\": ev[\"j\"],\n",
    "            })\n",
    "\n",
    "            cur_E = seg_end_E  # next segment starts at the previous segment's end\n",
