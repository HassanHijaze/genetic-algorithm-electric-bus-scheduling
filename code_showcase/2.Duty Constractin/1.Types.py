    "            unscheduled.append(ev)\n",
    "            continue\n",
    "\n",
    "        end_final, neg_credited, sid, grid_id, segments, segment_lanes = best\n",
    "\n",
    "        # --------- EXPORT: one event per contiguous segment ---------\n",
    "        # CLAMP start energy to last end-of-dwell for this duty (if any)\n",
    "        cur_E = float(ev[\"start_energy_float\"])\n",
