    "    # --- post-schedule SoC simulation + pruner (contention-aware) ---\n",
    "    def _simulate_duty_energy(\n",
    "        seq: List[int],\n",
    "        blocks, trips, dt_best, tt_best, td_best, d5_best, d6_best,\n",
    "        dwell_map: Dict[Tuple[int,int], dict],\n",
    "        battery_capacity: float,\n",
    "        final_reserve_wh: float = FINAL_DEPOT_RESERVE_WH,\n",
    "    ):\n",
    "        \"\"\"\n",
    "        Simulate one duty's SoC with real credited minutes from dwell_map.\n",
    "        Return (ok, failing_merge_pair or None, min_soc).\n",
    "        If infeasible in/after a merge (a->b), returns that (a,b) so we can prune it.\n",
    "        \"\"\"\n",
    "        E = float(battery_capacity)\n",
    "        minE = E\n",
    "\n",
    "        def e_trip(t): return float(trips[t].get(\"energy\", 0.0))\n",
    "        def e_tt(u,v):\n",
    "            dh = tt_best.get((u,v)); return float(dh[1].get(\"energy\",0.0)) if dh else float(\"inf\")\n",
    "        def e_dt(t): x=dt_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "        def e_td(t): x=td_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "        def e_d5(t): x=d5_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "        def e_d6(t): x=d6_best.get(t); return float(x[2].get(\"energy\",0.0)) if x else float(\"inf\")\n",
    "\n",
    "        # Start: type-1 + head trip of first block\n",
    "        head = blocks[seq[0]][0]\n",
    "        E -= (e_dt(head) + e_trip(head)); minE = min(minE, E)\n",
    "        if E < 0: return False, None, minE\n",
    "\n",
    "        # Walk merges\n",
    "        for a, b in zip(seq, seq[1:]):\n",
    "            # inside block a\n",
    "            ba = blocks[a]\n",
    "            for u,v in zip(ba, ba[1:]):\n",
    "                E -= (e_tt(u,v) + e_trip(v)); minE = min(minE, E)\n",
    "                if E < 0: return False, (a,b), minE  # split at this merge\n",
    "\n",
    "            # pull-in type-6\n",
    "            tail = ba[-1]\n",
    "            E -= e_d6(tail); minE = min(minE, E)\n",
    "            if E < 0: return False, (a,b), minE\n",
    "\n",
    "            # dwell energy (as actually placed)\n",
    "            ev = dwell_map.get((a,b))\n",
    "            if not ev: return False, (a,b), minE\n",
    "            E = min(battery_capacity, float(ev.get('end_energy_float', ev['end_energy'])))\n",
    "            minE = min(minE, E)\n",
    "\n",
    "            # pull-out type-5 + head of next block\n",
    "            head_next = blocks[b][0]\n",
    "            E -= (e_d5(head_next) + e_trip(head_next)); minE = min(minE, E)\n",
    "            if E < 0: return False, (a,b), minE\n",
    "\n",
    "        # tail block internals\n",
    "        last = seq[-1]\n",
    "        bl = blocks[last]\n",
    "        for u,v in zip(bl, bl[1:]):\n",
    "            E -= (e_tt(u,v) + e_trip(v)); minE = min(minE, E)\n",
    "            if E < 0:\n",
    "                if len(seq) >= 2:\n",
    "                    return False, (seq[-2], seq[-1]), minE\n",
    "                return False, None, minE\n",
    "\n",
    "        # final type-2 return; require reserve at arrival\n",
    "        tail = bl[-1]\n",
    "        E -= e_td(tail); minE = min(minE, E)\n",
    "        if E < final_reserve_wh:\n",
    "            if len(seq) >= 2:\n",
    "                return False, (seq[-2], seq[-1]), minE\n",
    "            return False, None, minE\n",
    "\n",
    "        return True, None, minE\n",
