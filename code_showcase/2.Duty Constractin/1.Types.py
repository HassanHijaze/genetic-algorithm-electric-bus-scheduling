    "def _hk_max_matching(U: List[int], V: List[int], adj: Dict[int, List[int]]):\n",
    "    \"\"\"Hopcroft–Karp bipartite maximum matching.\"\"\"\n",
    "\n",
    "    ## maximum matching ⇒ minimum number of chains/duties ⇒ fewer buses.\n",
    "    from collections import deque\n",
    "    INF = 10**18\n",
    "    pairU = {u: None for u in U}\n",
    "    pairV = {v: None for v in V}\n",
    "    dist: Dict[int, int] = {}\n",
    "\n",
    "\n",
    "    ##BFS finds the minimum possible length of any augmenting path and layers the graph.\n",
    "    def bfs() -> bool:\n",
    "        q = deque()\n",
    "        for u in U:\n",
    "            if pairU[u] is None:\n",
    "                dist[u] = 0\n",
    "                q.append(u)\n",
    "            else:\n",
    "                dist[u] = INF\n",
    "        found = False\n",
    "        while q:\n",
    "            u = q.popleft()\n",
    "            for v in adj.get(u, []):\n",
    "                pu = pairV[v]\n",
    "                if pu is None:\n",
    "                    found = True\n",
    "                else:\n",
    "                    if dist.get(pu, INF) == INF:\n",
    "                        dist[pu] = dist[u] + 1\n",
    "                        q.append(pu)\n",
    "        return found\n",
    "        \n",
    "    ##DFS then finds a maximal set of vertex-disjoint augmenting paths of that length in one phase.\n",
    "    \n",
    "    def dfs(u: int) -> bool:\n",
    "        for v in adj.get(u, []):\n",
    "            pu = pairV[v]\n",
    "            if pu is None or (dist.get(pu, INF) == dist[u] + 1 and dfs(pu)):\n",
    "                pairU[u] = v\n",
    "                pairV[v] = u\n",
    "                return True\n",
    "        dist[u] = INF\n",
    "        return False\n",
    "\n",
    "    while bfs():\n",
    "        for u in U:\n",
    "            if pairU[u] is None:\n",
    "                dfs(u)\n",
    "    return pairU, pairV\n",
