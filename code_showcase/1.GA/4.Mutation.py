    "def best_relocate_mutation(\n",
    "    chromosome: Chromosome,\n",
    "    trips: TripMap,\n",
    "    dt_best: DTBestMap,\n",
    "    tt_best: TTBestMap,\n",
    "    td_best: TDBestMap,\n",
    "    allow_equal: bool = True,\n",
    "    seed: Optional[int] = None,\n",
    ") -> Chromosome:\n",
    "    \"\"\"\n",
    "    Best-relocate mutation.\n",
    "\n",
    "    This operator matches the thesis description.\n",
    "\n",
    "    A trip x is selected and removed from its original chain. Then all possible\n",
    "    time-feasible reinsertion positions are evaluated.\n",
    "\n",
    "    For removing x from its original chain:\n",
    "\n",
    "        ΔLrem = L(a, b) - L(a, x) - L(x, b)\n",
    "\n",
    "    For inserting x into a target chain:\n",
    "\n",
    "        ΔLins = L(u, x) + L(x, v) - L(u, v)\n",
    "\n",
    "    The total change in proxy deadhead distance is:\n",
    "\n",
    "        ΔL = ΔLins + ΔLrem\n",
    "\n",
    "    The best reinsertion position is the feasible position with the smallest ΔL.\n",
    "    The relocation is accepted only if this minimum ΔL is strictly negative.\n",
    "    Otherwise, the chromosome is returned unchanged.\n",
    "    \"\"\"\n",
    "\n",
    "    rng = random.Random(seed)\n",
    "    original = copy_chromosome(chromosome)\n",
    "    child = copy_chromosome(chromosome)\n",
    "\n",
    "    trip_positions: List[Tuple[int, int]] = [\n",
    "        (chain_index, position)\n",
    "        for chain_index, chain in enumerate(child)\n",
    "        for position in range(len(chain))\n",
    "    ]\n",
    "\n",
    "    if not trip_positions:\n",
    "        return child\n",
    "\n",
    "    source_chain_index, source_position = rng.choice(trip_positions)\n",
    "\n",
    "    source_chain = child[source_chain_index]\n",
    "    trip_id = source_chain[source_position]\n",
    "\n",
    "    delta_remove = removal_delta_length(\n",
    "        chain=source_chain,\n",
    "        position=source_position,\n",
    "        tt_best=tt_best,\n",
    "        dt_best=dt_best,\n",
    "        td_best=td_best,\n",
    "    )\n",
    "\n",
    "    # Remove x from its original position.\n",
    "    source_chain.pop(source_position)\n",
    "\n",
    "    # Remove empty source chain if x was the only trip in the chain.\n",
    "    if not source_chain:\n",
    "        child.pop(source_chain_index)\n",
    "\n",
    "    best_delta: Optional[float] = None\n",
    "    best_chain_index: Optional[int] = None\n",
    "    best_position: Optional[int] = None\n",
    "\n",
    "    # Test all feasible reinsertion positions in all existing chains.\n",
    "    for target_chain_index, target_chain in enumerate(child):\n",
    "        for insertion_position in range(len(target_chain) + 1):\n",
    "            if not is_insertion_time_feasible(\n",
    "                trips=trips,\n",
    "                tt_best=tt_best,\n",
    "                chain=target_chain,\n",
    "                position=insertion_position,\n",
    "                trip_id=trip_id,\n",
    "                allow_equal=allow_equal,\n",
    "            ):\n",
    "                continue\n",
    "\n",
    "            delta_insert = insertion_delta_length(\n",
    "                chain=target_chain,\n",
    "                position=insertion_position,\n",
    "                trip_id=trip_id,\n",
    "                tt_best=tt_best,\n",
    "                dt_best=dt_best,\n",
    "                td_best=td_best,\n",
    "            )\n",
    "\n",
    "            total_delta = delta_remove + delta_insert\n",
    "\n",
    "            if best_delta is None or total_delta < best_delta:\n",
    "                best_delta = total_delta\n",
    "                best_chain_index = target_chain_index\n",
    "                best_position = insertion_position\n",
    "\n",
    "    # Also test inserting x as a new singleton chain.\n",
    "    empty_chain: List[int] = []\n",
    "\n",
    "    delta_insert_singleton = insertion_delta_length(\n",
    "        chain=empty_chain,\n",
    "        position=0,\n",
    "        trip_id=trip_id,\n",
    "        tt_best=tt_best,\n",
    "        dt_best=dt_best,\n",
    "        td_best=td_best,\n",
    "    )\n",
    "\n",
    "    total_delta_singleton = delta_remove + delta_insert_singleton\n",
    "\n",
    "    if best_delta is None or total_delta_singleton < best_delta:\n",
    "        best_delta = total_delta_singleton\n",
    "        best_chain_index = len(child)\n",
    "        best_position = 0\n",
    "\n",
    "    # Accept the relocation only if it improves the proxy distance.\n",
    "    if (\n",
    "        best_delta is not None\n",
    "        and best_chain_index is not None\n",
    "        and best_position is not None\n",
    "        and best_delta < 0.0\n",
    "    ):\n",
    "        if best_chain_index == len(child):\n",
    "            child.append([trip_id])\n",
    "        else:\n",
    "            child[best_chain_index].insert(best_position, trip_id)\n",
    "\n",
    "        return remove_empty_chains(child)\n",
    "\n",
    "    # No improving relocation exists.\n",
    "    return original\n",
    "\n"
