    "def remove_empty_chains(chromosome: Chromosome) -> Chromosome:\n",
    "    \"\"\"\n",
    "    Remove empty chains from a chromosome.\n",
    "    \"\"\"\n",
    "\n",
    "    return [chain for chain in chromosome if chain]\n",
