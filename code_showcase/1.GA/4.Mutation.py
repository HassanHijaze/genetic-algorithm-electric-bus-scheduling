    "from __future__ import annotations\n",
    "\n",
    "import random\n",
    "from typing import Dict, List, Optional, Sequence, Tuple\n",
    "\n",
    "\n",
    "TripMap = Dict[int, dict]\n",
    "TTBestMap = Dict[Tuple[int, int], Tuple[int, dict]]\n",
    "DTBestMap = Dict[int, Tuple[int, int, dict]]\n",
    "TDBestMap = Dict[int, Tuple[int, int, dict]]\n",
    "\n",
    "Chromosome = List[List[int]]\n",
    "\n",
    "INF = 10**18\n",
    "\n",
    "\n",
    "def copy_chromosome(chromosome: Chromosome) -> Chromosome:\n",
    "    \"\"\"\n",
    "    Return a deep copy of a chromosome.\n",
    "    \"\"\"\n",
    "\n",
    "    return [list(chain) for chain in chromosome]\n",
    "\n",
    "\n",
    "def tt_duration(\n",
    "    tt_best: TTBestMap,\n",
    "    u: int,\n",
    "    v: int,\n",
    ") -> int:\n",
    "    \"\"\"\n",
    "    Return trip-to-trip deadhead duration for edge u -> v.\n",
    "    \"\"\"\n",
    "\n",
    "    item = tt_best.get((u, v))\n",
    "\n",
    "    if item is None:\n",
    "        return INF\n",
    "\n",
    "    _, deadhead = item\n",
    "    return int(deadhead.get(\"duration\", INF))\n",
    "\n",
    "\n",
    "def tt_length(\n",
    "    tt_best: TTBestMap,\n",
    "    u: int,\n",
    "    v: int,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Return trip-to-trip deadhead distance for edge u -> v.\n",
    "    \"\"\"\n",
    "\n",
    "    item = tt_best.get((u, v))\n",
    "\n",
    "    if item is None:\n",
    "        return float(\"inf\")\n",
    "\n",
    "    _, deadhead = item\n",
    "    return float(deadhead.get(\"length\", float(\"inf\")))\n",
    "\n",
    "\n",
    "def dt_length(\n",
    "    dt_best: DTBestMap,\n",
    "    v: int,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Return depot-to-trip deadhead distance for edge depot -> v.\n",
    "    \"\"\"\n",
    "\n",
    "    item = dt_best.get(v)\n",
    "\n",
    "    if item is None:\n",
    "        return float(\"inf\")\n",
    "\n",
    "    _, _, deadhead = item\n",
    "    return float(deadhead.get(\"length\", float(\"inf\")))\n",
    "\n",
    "\n",
    "def td_length(\n",
    "    td_best: TDBestMap,\n",
    "    u: int,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Return trip-to-depot deadhead distance for edge u -> depot.\n",
    "    \"\"\"\n",
    "\n",
    "    item = td_best.get(u)\n",
    "\n",
    "    if item is None:\n",
    "        return float(\"inf\")\n",
    "\n",
    "    _, _, deadhead = item\n",
    "    return float(deadhead.get(\"length\", float(\"inf\")))\n",
    "\n",
    "\n",
    "def edge_length(\n",
    "    left: Optional[int],\n",
    "    right: Optional[int],\n",
    "    tt_best: TTBestMap,\n",
    "    dt_best: DTBestMap,\n",
    "    td_best: TDBestMap,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Return the deadhead distance of an edge.\n",
    "\n",
    "    Cases:\n",
    "        left is None, right is trip:\n",
    "            depot -> right\n",
    "\n",
    "        left is trip, right is None:\n",
    "            left -> depot\n",
    "\n",
    "        left is trip, right is trip:\n",
    "            left -> right\n",
    "\n",
    "        left is None, right is None:\n",
    "            empty chain case\n",
    "    \"\"\"\n",
    "\n",
    "    if left is None and right is None:\n",
    "        return 0.0\n",
    "\n",
    "    if left is None:\n",
    "        return dt_length(dt_best, right)\n",
    "\n",
    "    if right is None:\n",
    "        return td_length(td_best, left)\n",
    "\n",
    "    return tt_length(tt_best, left, right)\n",
    "\n",
    "\n",
    "def is_time_feasible_pair(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    u: int,\n",
    "    v: int,\n",
    "    allow_equal: bool = True,\n",
    ") -> bool:\n",
    "    \"\"\"\n",
    "    Check whether trip v can follow trip u in time.\n",
    "    \"\"\"\n",
    "\n",
    "    if u not in trips or v not in trips:\n",
    "        return False\n",
    "\n",
    "    duration = tt_duration(tt_best, u, v)\n",
    "\n",
    "    if duration >= INF:\n",
    "        return False\n",
    "\n",
    "    arrival_time = int(trips[u][\"end\"]) + duration\n",
    "    start_v = int(trips[v][\"start\"])\n",
    "\n",
    "    if allow_equal:\n",
    "        return arrival_time <= start_v\n",
    "\n",
    "    return arrival_time < start_v\n",
    "\n",
    "\n",
    "def is_insertion_time_feasible(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    chain: Sequence[int],\n",
    "    position: int,\n",
    "    trip_id: int,\n",
    "    allow_equal: bool = True,\n",
    ") -> bool:\n",
    "    \"\"\"\n",
    "    Check whether trip_id can be inserted into a chain at the given position.\n",
    "\n",
    "    position = 0:\n",
    "        insert before the first trip\n",
    "\n",
    "    position = len(chain):\n",
    "        insert after the last trip\n",
    "\n",
    "    position between 0 and len(chain):\n",
    "        insert between two trips\n",
    "    \"\"\"\n",
    "\n",
    "    left = chain[position - 1] if position > 0 else None\n",
    "    right = chain[position] if position < len(chain) else None\n",
    "\n",
    "    if left is not None:\n",
    "        if not is_time_feasible_pair(\n",
    "            trips=trips,\n",
    "            tt_best=tt_best,\n",
    "            u=left,\n",
    "            v=trip_id,\n",
    "            allow_equal=allow_equal,\n",
    "        ):\n",
    "            return False\n",
    "\n",
    "    if right is not None:\n",
    "        if not is_time_feasible_pair(\n",
    "            trips=trips,\n",
    "            tt_best=tt_best,\n",
    "            u=trip_id,\n",
    "            v=right,\n",
    "            allow_equal=allow_equal,\n",
    "        ):\n",
    "            return False\n",
    "\n",
    "    return True\n",
    "\n",
