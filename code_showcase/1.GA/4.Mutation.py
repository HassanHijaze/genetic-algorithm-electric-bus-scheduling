    "def removal_delta_length(\n",
    "    chain: Sequence[int],\n",
    "    position: int,\n",
    "    tt_best: TTBestMap,\n",
    "    dt_best: DTBestMap,\n",
    "    td_best: TDBestMap,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Compute ΔLrem for removing x from its original chain.\n",
    "\n",
    "    If x is removed from between two consecutive elements (a, x, b),\n",
    "    then edges (a, x) and (x, b) are replaced by (a, b):\n",
    "\n",
    "        ΔLrem = L(a, b) - L(a, x) - L(x, b)\n",
    "\n",
    "    This function also handles depot analogues:\n",
    "\n",
    "        x at beginning:\n",
    "            depot -> x -> b becomes depot -> b\n",
    "\n",
    "        x at end:\n",
    "            a -> x -> depot becomes a -> depot\n",
    "\n",
    "        x alone:\n",
    "            depot -> x -> depot becomes empty chain\n",
    "    \"\"\"\n",
    "\n",
    "    x = chain[position]\n",
    "\n",
    "    left = chain[position - 1] if position > 0 else None\n",
    "    right = chain[position + 1] if position + 1 < len(chain) else None\n",
    "\n",
    "    old_length = (\n",
    "        edge_length(left, x, tt_best, dt_best, td_best)\n",
    "        + edge_length(x, right, tt_best, dt_best, td_best)\n",
    "    )\n",
    "\n",
    "    new_length = edge_length(left, right, tt_best, dt_best, td_best)\n",
    "\n",
    "    return new_length - old_length\n",
    "\n",
    "\n",
    "def insertion_delta_length(\n",
    "    chain: Sequence[int],\n",
    "    position: int,\n",
    "    trip_id: int,\n",
    "    tt_best: TTBestMap,\n",
    "    dt_best: DTBestMap,\n",
    "    td_best: TDBestMap,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Compute ΔLins for inserting x into a target chain.\n",
    "\n",
    "    If x is inserted between two consecutive elements (u, v),\n",
    "    then edge (u, v) is replaced by (u, x) and (x, v):\n",
    "\n",
    "        ΔLins = L(u, x) + L(x, v) - L(u, v)\n",
    "\n",
    "    This function also handles depot analogues:\n",
    "\n",
    "        insert at beginning:\n",
    "            depot -> v becomes depot -> x -> v\n",
    "\n",
    "        insert at end:\n",
    "            u -> depot becomes u -> x -> depot\n",
    "\n",
    "        insert into empty chain:\n",
    "            empty chain becomes depot -> x -> depot\n",
    "    \"\"\"\n",
    "\n",
    "    left = chain[position - 1] if position > 0 else None\n",
    "    right = chain[position] if position < len(chain) else None\n",
    "\n",
    "    old_length = edge_length(left, right, tt_best, dt_best, td_best)\n",
    "\n",
    "    new_length = (\n",
    "        edge_length(left, trip_id, tt_best, dt_best, td_best)\n",
    "        + edge_length(trip_id, right, tt_best, dt_best, td_best)\n",
    "    )\n",
    "\n",
    "    return new_length - old_length\n",
    "\n",
