    "def would_create_cycle(\n",
    "    successor: Dict[int, int],\n",
    "    i: int,\n",
    "    j: int,\n",
    ") -> bool:\n",
    "    \"\"\"\n",
    "    Check whether adding edge i -> j would create a cycle.\n",
    "    \"\"\"\n",
    "\n",
    "    current = j\n",
    "\n",
    "    while current in successor:\n",
    "        current = successor[current]\n",
    "\n",
    "        if current == i:\n",
    "            return True\n",
