    "def build_chains_from_successors(\n",
    "    all_trip_ids: Sequence[int],\n",
    "    successor: Dict[int, int],\n",
    "    predecessor: Dict[int, int],\n",
    "    trips: TripMap,\n",
    ") -> Chromosome:\n",
    "    \"\"\"\n",
    "    Convert successor/predecessor dictionaries into trip chains.\n",
    "    \"\"\"\n",
    "\n",
    "    used: Set[int] = set()\n",
    "    chromosome: Chromosome = []\n",
    "\n",
    "    ordered_trips = sorted(\n",
    "        [int(t) for t in all_trip_ids],\n",
    "        key=lambda t: (\n",
    "            int(trips[t][\"start\"]),\n",
    "            int(trips[t][\"end\"]),\n",
    "            t,\n",
    "        ),\n",
    "    )\n",
    "\n",
    "    for trip_id in ordered_trips:\n",
    "        if trip_id in used:\n",
    "            continue\n",
    "\n",
    "        if trip_id in predecessor:\n",
    "            continue\n",
    "\n",
    "        chain: List[int] = []\n",
    "        current = trip_id\n",
    "\n",
    "        while current not in used:\n",
    "            chain.append(current)\n",
    "            used.add(current)\n",
    "\n",
    "            if current not in successor:\n",
    "                break\n",
    "\n",
    "            current = successor[current]\n",
    "\n",
    "        if chain:\n",
    "            chromosome.append(chain)\n",
    "\n",
    "    # Add any missing trips as singleton chains.\n",
    "    for trip_id in ordered_trips:\n",
    "        if trip_id not in used:\n",
    "            chromosome.append([trip_id])\n",
    "            used.add(trip_id)\n",
    "\n",
    "    return chromosome\n",
