    "def insert_remaining_trips(\n",
    "    chromosome: Chromosome,\n",
    "    all_trip_ids: Sequence[int],\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    beta_wait: float = 0.005,\n",
    "    allow_equal: bool = True,\n",
    ") -> Chromosome:\n",
    "    \"\"\"\n",
    "    Insert trips that were not inherited through parent edges.\n",
    "\n",
    "    Each remaining trip is appended to the feasible chain that gives the lowest\n",
    "    edge cost. If no feasible insertion exists, a new singleton chain is created.\n",
    "    \"\"\"\n",
    "\n",
    "    used = {trip for chain in chromosome for trip in chain}\n",
    "\n",
    "    remaining = sorted(\n",
    "        [int(t) for t in all_trip_ids if int(t) not in used],\n",
    "        key=lambda t: (\n",
    "            int(trips[t][\"start\"]),\n",
    "            int(trips[t][\"end\"]),\n",
    "            t,\n",
    "        ),\n",
    "    )\n",
    "\n",
    "    for trip_id in remaining:\n",
    "        best_chain_index: Optional[int] = None\n",
    "        best_score: Optional[float] = None\n",
    "\n",
    "        for chain_index, chain in enumerate(chromosome):\n",
    "            tail = chain[-1]\n",
    "\n",
    "            if not is_time_feasible(trips, tt_best, tail, trip_id, allow_equal):\n",
    "                continue\n",
    "\n",
    "            score = edge_cost(\n",
    "                trips=trips,\n",
    "                tt_best=tt_best,\n",
    "                i=tail,\n",
    "                j=trip_id,\n",
    "                beta_wait=beta_wait,\n",
    "            )\n",
    "\n",
    "            if best_score is None or score < best_score:\n",
    "                best_score = score\n",
    "                best_chain_index = chain_index\n",
    "\n",
    "        if best_chain_index is None:\n",
    "            chromosome.append([trip_id])\n",
    "        else:\n",
    "            chromosome[best_chain_index].append(trip_id)\n",
    "\n",
    "    chromosome.sort(\n",
    "        key=lambda chain: (\n",
    "            int(trips[chain[0]][\"start\"]),\n",
    "            int(trips[chain[0]][\"end\"]),\n",
    "            chain[0],\n",
    "        )\n",
    "    )\n",
    "\n",
    "    return chromosome\n",
    "\n",
