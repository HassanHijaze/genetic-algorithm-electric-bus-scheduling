    "from __future__ import annotations\n",
    "\n",
    "import random\n",
    "from typing import Dict, List, Optional, Sequence, Set, Tuple\n",
    "\n",
    "\n",
    "TripMap = Dict[int, dict]\n",
    "TTBestMap = Dict[Tuple[int, int], Tuple[int, dict]]\n",
    "Chromosome = List[List[int]]\n",
    "\n",
    "\n",
    "def copy_chromosome(chromosome: Chromosome) -> Chromosome:\n",
    "    \"\"\"Return a deep copy of a chromosome.\"\"\"\n",
    "\n",
    "    return [list(chain) for chain in chromosome]\n",
    "\n",
    "\n",
    "def tt_duration_length(\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    j: int,\n",
    ") -> Tuple[int, float]:\n",
    "    \"\"\"\n",
    "    Return deadhead duration and distance for connection i -> j.\n",
    "    \"\"\"\n",
    "\n",
    "    item = tt_best.get((i, j))\n",
    "\n",
    "    if item is None:\n",
    "        return 10**12, 1e18\n",
    "\n",
    "    _, deadhead = item\n",
    "\n",
    "    duration = int(deadhead.get(\"duration\", 10**12))\n",
    "    length = float(deadhead.get(\"length\", 1e18))\n",
    "\n",
    "    return duration, length\n",
    "\n",
    "\n",
    "def is_time_feasible(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    j: int,\n",
    "    allow_equal: bool = True,\n",
    ") -> bool:\n",
    "    \"\"\"\n",
    "    Check whether trip j can follow trip i.\n",
    "    \"\"\"\n",
    "\n",
    "    if i not in trips or j not in trips:\n",
    "        return False\n",
    "\n",
    "    duration, _ = tt_duration_length(tt_best, i, j)\n",
    "\n",
    "    if duration >= 10**12:\n",
    "        return False\n",
    "\n",
    "    arrival_time = int(trips[i][\"end\"]) + duration\n",
    "    start_j = int(trips[j][\"start\"])\n",
    "\n",
    "    if allow_equal:\n",
    "        return arrival_time <= start_j\n",
    "\n",
    "    return arrival_time < start_j\n",
    "\n",
    "\n",
    "def edge_waiting_time(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    j: int,\n",
    ") -> int:\n",
    "    \"\"\"\n",
    "    Compute waiting time W(i,j).\n",
    "\n",
    "    W(i,j) = max(0, start(j) - (end(i) + tau(i,j)))\n",
    "    \"\"\"\n",
    "\n",
    "    duration, _ = tt_duration_length(tt_best, i, j)\n",
    "\n",
    "    arrival_time = int(trips[i][\"end\"]) + duration\n",
    "    start_j = int(trips[j][\"start\"])\n",
    "\n",
    "    return max(0, start_j - arrival_time)\n",
    "\n",
    "\n",
    "def edge_cost(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    j: int,\n",
    "    beta_wait: float = 0.005,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Compute crossover edge cost:\n",
    "\n",
    "        c(i,j) = L(i,j) + beta_wait * W(i,j)\n",
    "\n",
    "    where:\n",
    "        L(i,j) is deadhead distance,\n",
    "        W(i,j) is waiting time.\n",
    "    \"\"\"\n",
    "\n",
    "    _, length = tt_duration_length(tt_best, i, j)\n",
    "    wait = edge_waiting_time(trips, tt_best, i, j)\n",
    "\n",
    "    return length + beta_wait * wait\n",
    "\n",
    "\n",
    "def extract_parent_edges(\n",
    "    chromosome: Chromosome,\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    allow_equal: bool = True,\n",
    ") -> List[Tuple[int, int]]:\n",
    "    \"\"\"\n",
    "    Extract all consecutive trip pairs from a parent chromosome.\n",
    "    \"\"\"\n",
    "\n",
    "    edges: List[Tuple[int, int]] = []\n",
    "\n",
    "    for chain in chromosome:\n",
    "        for i, j in zip(chain, chain[1:]):\n",
    "            i = int(i)\n",
    "            j = int(j)\n",
    "\n",
    "            if is_time_feasible(trips, tt_best, i, j, allow_equal):\n",
    "                edges.append((i, j))\n",
    "\n",
    "    return edges\n",
