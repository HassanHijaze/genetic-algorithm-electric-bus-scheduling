    "def edge_preserving_crossover(\n",
    "    parent1: Chromosome,\n",
    "    parent2: Chromosome,\n",
    "    all_trip_ids: Sequence[int],\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    beta_wait: float = 0.005,\n",
    "    allow_equal: bool = True,\n",
    "    seed: Optional[int] = None,\n",
    ") -> Chromosome:\n",
    "    \"\"\"\n",
    "    Edge-preserving crossover for time-feasible trip-chain chromosomes.\n",
    "\n",
    "    The operator follows the thesis description:\n",
    "\n",
    "    1. Extract consecutive trip pairs from both parents.\n",
    "    2. Score each edge by:\n",
    "\n",
    "           c(i,j) = L(i,j) + beta_wait * W(i,j)\n",
    "\n",
    "       where L is deadhead distance and W is waiting time.\n",
    "\n",
    "    3. Sort candidate edges by increasing cost.\n",
    "    4. Add edges to the child if:\n",
    "       - the tail has no successor yet,\n",
    "       - the head has no predecessor yet,\n",
    "       - the edge is time-feasible,\n",
    "       - adding the edge does not create a cycle.\n",
    "\n",
    "    5. Convert the selected edges into chains.\n",
    "    6. Insert trips not covered by inherited edges.\n",
    "    \"\"\"\n",
    "\n",
    "    rng = random.Random(seed)\n",
    "\n",
    "    edges_parent1 = extract_parent_edges(parent1, trips, tt_best, allow_equal)\n",
    "    edges_parent2 = extract_parent_edges(parent2, trips, tt_best, allow_equal)\n",
    "\n",
    "    candidate_edges = list(set(edges_parent1) | set(edges_parent2))\n",
    "\n",
    "    rng.shuffle(candidate_edges)\n",
    "\n",
    "    candidate_edges.sort(\n",
    "        key=lambda edge: edge_cost(\n",
    "            trips=trips,\n",
    "            tt_best=tt_best,\n",
    "            i=edge[0],\n",
    "            j=edge[1],\n",
    "            beta_wait=beta_wait,\n",
    "        )\n",
    "    )\n",
    "\n",
    "    successor: Dict[int, int] = {}\n",
    "    predecessor: Dict[int, int] = {}\n",
    "\n",
    "    for i, j in candidate_edges:\n",
    "        if i in successor:\n",
    "            continue\n",
    "\n",
    "        if j in predecessor:\n",
    "            continue\n",
    "\n",
    "        if not is_time_feasible(trips, tt_best, i, j, allow_equal):\n",
    "            continue\n",
    "\n",
    "        if would_create_cycle(successor, i, j):\n",
    "            continue\n",
    "\n",
    "        successor[i] = j\n",
    "        predecessor[j] = i\n",
    "\n",
    "    child = build_chains_from_successors(\n",
    "        all_trip_ids=all_trip_ids,\n",
    "        successor=successor,\n",
    "        predecessor=predecessor,\n",
    "        trips=trips,\n",
    "    )\n",
    "\n",
    "    child = insert_remaining_trips(\n",
    "        chromosome=child,\n",
    "        all_trip_ids=all_trip_ids,\n",
    "        trips=trips,\n",
    "        tt_best=tt_best,\n",
    "        beta_wait=beta_wait,\n",
    "        allow_equal=allow_equal,\n",
    "    )\n",
    "\n",
    "    return child\n",
    "\n",
