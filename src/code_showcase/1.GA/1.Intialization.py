    "def initialize_chromosome(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    alpha: float = 1.0,\n",
    "    beta: float = 1e-4,\n",
    "    gamma: float = 0.10,\n",
    "    epsilon: float = 0.10,\n",
    "    delta: float = 0.0,\n",
    "    allow_equal: bool = True,\n",
    "    rng: Optional[random.Random] = None,\n",
    ") -> Chromosome:\n",
    "    \"\"\"\n",
    "    Construct one initial chromosome using the thesis initialization procedure.\n",
    "\n",
    "    The heuristic repeatedly selects the earliest unassigned trip as a seed and\n",
    "    greedily appends feasible successors while maintaining time feasibility.\n",
    "\n",
    "    Each feasible successor v after current trip i is scored by:\n",
    "\n",
    "        s(i, v) = alpha * dur(i, v)\n",
    "                + beta  * energy(i, v)\n",
    "                + gamma * idle(i, v)\n",
    "\n",
    "    Independent jitter is added to the score, and epsilon-greedy selection is\n",
    "    used to balance exploitation and exploration.\n",
    "    \"\"\"\n",
    "\n",
    "    rng = rng or random.Random()\n",
    "\n",
    "    unassigned: Set[int] = set(trips.keys())\n",
    "\n",
    "    ordered_trips = sorted(\n",
    "        trips.keys(),\n",
    "        key=lambda t: (\n",
    "            int(trips[t][\"start\"]),\n",
    "            int(trips[t][\"end\"]),\n",
    "            t,\n",
    "        ),\n",
    "    )\n",
    "\n",
    "    chromosome: Chromosome = []\n",
    "\n",
    "    while unassigned:\n",
    "        # Select a seed among the earliest unassigned trips.\n",
    "        seed = next(t for t in ordered_trips if t in unassigned)\n",
    "\n",
    "        chain = [seed]\n",
    "        unassigned.remove(seed)\n",
    "\n",
    "        current = seed\n",
    "\n",
    "        while True:\n",
    "            feasible_candidates = [\n",
    "                v\n",
    "                for v in unassigned\n",
    "                if is_time_feasible(\n",
    "                    trips=trips,\n",
    "                    tt_best=tt_best,\n",
    "                    i=current,\n",
    "                    v=v,\n",
    "                    allow_equal=allow_equal,\n",
    "                )\n",
    "            ]\n",
    "\n",
    "            if not feasible_candidates:\n",
    "                break\n",
    "\n",
    "            scored_candidates: List[Tuple[float, int]] = []\n",
    "\n",
    "            for v in feasible_candidates:\n",
    "                score = score_successor(\n",
    "                    trips=trips,\n",
    "                    tt_best=tt_best,\n",
    "                    i=current,\n",
    "                    v=v,\n",
    "                    alpha=alpha,\n",
    "                    beta=beta,\n",
    "                    gamma=gamma,\n",
    "                )\n",
    "\n",
    "                jitter = rng.uniform(-delta, delta) if delta > 0 else 0.0\n",
    "                perturbed_score = score + jitter\n",
    "\n",
    "                scored_candidates.append((perturbed_score, v))\n",
    "\n",
    "            u = rng.random()\n",
    "\n",
    "            if u < epsilon:\n",
    "                # Exploration\n",
    "                selected = rng.choice(feasible_candidates)\n",
    "            else:\n",
    "                # Exploitation\n",
    "                selected = min(scored_candidates, key=lambda item: item[0])[1]\n",
    "\n",
    "            chain.append(selected)\n",
    "            unassigned.remove(selected)\n",
    "            current = selected\n",
    "\n",
    "        chromosome.append(chain)\n",
    "\n",
    "    return chromosome\n",
