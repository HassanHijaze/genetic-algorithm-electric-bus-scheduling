    "from __future__ import annotations\n",
    "\n",
    "import random\n",
    "from typing import Dict, List, Tuple, Optional, Set\n",
    "\n",
    "\n",
    "TripMap = Dict[int, dict]\n",
    "TTBestMap = Dict[Tuple[int, int], Tuple[int, dict]]\n",
    "Chromosome = List[List[int]]\n",
    "\n",
    "\n",
    "def tt_energy_duration(\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    v: int,\n",
    ") -> Tuple[float, int]:\n",
    "    \"\"\"\n",
    "    Return deadhead energy and duration for connection i -> v.\n",
    "    \"\"\"\n",
    "\n",
    "    item = tt_best.get((i, v))\n",
    "\n",
    "    if item is None:\n",
    "        return float(\"inf\"), 10**12\n",
    "\n",
    "    _, deadhead = item\n",
    "\n",
    "    energy = float(deadhead.get(\"energy\", float(\"inf\")))\n",
    "    duration = int(deadhead.get(\"duration\", 10**12))\n",
    "\n",
    "    return energy, duration\n",
    "\n",
    "\n",
    "def is_time_feasible(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    v: int,\n",
    "    allow_equal: bool = True,\n",
    ") -> bool:\n",
    "    \"\"\"\n",
    "    Check whether trip v can follow trip i.\n",
    "    \"\"\"\n",
    "\n",
    "    if i not in trips or v not in trips:\n",
    "        return False\n",
    "\n",
    "    energy, duration = tt_energy_duration(tt_best, i, v)\n",
    "\n",
    "    if energy == float(\"inf\"):\n",
    "        return False\n",
    "\n",
    "    arrival_time = int(trips[i][\"end\"]) + duration\n",
    "    start_v = int(trips[v][\"start\"])\n",
    "\n",
    "    if allow_equal:\n",
    "        return arrival_time <= start_v\n",
    "\n",
    "    return arrival_time < start_v\n",
    "\n",
    "\n",
    "def idle_time(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    v: int,\n",
    ") -> int:\n",
    "    \"\"\"\n",
    "    Return waiting time before trip v starts.\n",
    "    \"\"\"\n",
    "\n",
    "    _, duration = tt_energy_duration(tt_best, i, v)\n",
    "\n",
    "    arrival_time = int(trips[i][\"end\"]) + duration\n",
    "    start_v = int(trips[v][\"start\"])\n",
    "\n",
    "    return max(0, start_v - arrival_time)\n",
    "\n",
    "\n",
    "def score_successor(\n",
    "    trips: TripMap,\n",
    "    tt_best: TTBestMap,\n",
    "    i: int,\n",
    "    v: int,\n",
    "    alpha: float,\n",
    "    beta: float,\n",
    "    gamma: float,\n",
    ") -> float:\n",
    "    \"\"\"\n",
    "    Compute the initialization score:\n",
    "\n",
    "        s(i, v) = alpha * dur(i, v)\n",
    "                + beta  * energy(i, v)\n",
    "                + gamma * idle(i, v)\n",
    "\n",
    "    Duration and idle time are measured in minutes.\n",
    "    Energy is measured in Wh.\n",
    "    \"\"\"\n",
    "\n",
    "    energy, duration_seconds = tt_energy_duration(tt_best, i, v)\n",
    "    idle_seconds = idle_time(trips, tt_best, i, v)\n",
    "\n",
    "    duration_minutes = duration_seconds / 60.0\n",
    "    idle_minutes = idle_seconds / 60.0\n",
    "\n",
    "    return (\n",
    "        alpha * duration_minutes\n",
    "        + beta * energy\n",
    "        + gamma * idle_minutes\n",
    "    )\n",
    "\n",
