    "                if delta < best_delta:\n",
    "                    best_delta = delta\n",
    "                    best_chain_idx = chain_idx\n",
    "                    best_pos = pos\n",
    "\n",
    "        # Insert at the best feasible position\n",
    "        if best_chain_idx is not None and math.isfinite(best_delta):\n",
    "            partial_chains[best_chain_idx].insert(best_pos, trip)\n",
    "        else:\n",
    "            # If no feasible insertion exists in the current chains,\n",
    "            # create a new chain for this trip.\n",
    "            partial_chains.append([trip])\n",
    "\n",
    "    return [chain for chain in partial_chains if chain]"
